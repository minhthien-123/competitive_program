const int maxn = 30;
const ll inf = 1e18;
const int mod = 1e9 + 7;
const int inv = (mod + 1) / 2;
const int lg = 20;

ll add(ll x, ll y)
{
    return (x + y + 2 * mod) % mod;
}
ll sub(ll x, ll y)
{
    return (x - y + 2 * mod) % mod;
}
ll mul(ll x, ll y)
{
    return (x * y) % mod;
}
ll power(ll x, ll y)
{
    if (y == 0)
    {
        return 1;
    }
    ll tmp = power(x, y / 2);
    if (y % 2 == 0)
    {
        return (tmp * tmp) % mod;
    }
    else
    {
        return tmp * tmp % mod * x % mod;
    }
}

std::vector<int> adj[maxn + 7];
int n;
std::vector<int> tmp;
std::vector<std::vector<int>> ans;

void Try(int sum = n, int lim = n)
{
    if (sum == 0)
    {
        ans.pb(tmp);
        return;
    }

    for (int i = std::min(sum, lim); i >= 1; i--)
    {
        tmp.pb(i);
        Try(sum - i, i);
        tmp.pop_back();
    }
}

