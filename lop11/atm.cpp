ll ans = 0;

void TryX(int i = 1, int lim = n / 2, ll sum = 0)
{
    if (sum > m)
    {
        return;
    }
    if (i > lim)
    {
        v.pb(sum);
    }
    else
    {
        TryX(i + 1, lim, sum);
        TryX(i + 1, lim, sum + a[i]);
    }
}

void TryY(int i = n / 2 + 1, ll sum = 0)
{
    if (sum > m)
    {
        return;
    }
    if (i > n)
    {
        ans += std::upper_bound(v.begin(), v.end(), m - sum) - std::lower_bound(v.begin(), v.end(), m - sum);
    }
    else
    {
        TryY(i + 1, sum);
        TryY(i + 1, sum + a[i]);
    }
}

