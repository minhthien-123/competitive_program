ll add(ll x, ll y) { return (x + y + 2 * mod) % mod; }
ll sub(ll x, ll y) { return (x - y + 2 * mod) % mod; }
ll mul(ll x, ll y) { return (x * y) % mod; }
ll power(ll x, ll y)
{
    if (y == 0)
        return 1;
    ll tmp = power(x, y / 2);
    if (y % 2 == 0)
        return (tmp * tmp) % mod;
    else
        return tmp * tmp % mod * x % mod;
}

std::vector<int> adj[maxn + 7];
int c[maxn + 7];
int n, m;

bool bfs(int s)
{
    std::queue<int> qu;
    qu.push(s);
    c[s] = 0;

    while (sz(qu))
    {
        int u = qu.front();
        qu.pop();

        for (int v : adj[u])
        {
            if (c[v] == -1)
            {
                c[v] = 1 - c[u];
                qu.push(v);
            }
            else if (c[v] == c[u])
            {
                return false;
            }
        }
    }
    return true;
}

