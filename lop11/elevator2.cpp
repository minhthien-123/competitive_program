const int mod = 1e9 + 7;
const int inv = (mod + 1) / 2;
const int lg = 20;

ll add(ll x, ll y)
{
    return (x + y + 2 * mod) % mod;
}
ll sub(ll x, ll y)
{
    return (x - y + 2 * mod) % mod;
}
ll mul(ll x, ll y)
{
    return (x * y) % mod;
}
ll power(ll x, ll y)
{
    if (y == 0)
    {
        return 1;
    }
    ll tmp = power(x, y / 2);
    if (y % 2 == 0)
    {
        return (tmp * tmp) % mod;
    }
    else
    {
        return tmp * tmp % mod * x % mod;
    }
}

std::vector<int> adj[maxn + 7];
int n, m;
int a[maxn + 7];

bool check(ll mid)
{
    int w = 0;
    int cnt = 0;
    for (int i = 1; i <= n; i++)
    {
        w += a[i];
        if (w > mid)
        {
            if (a[i] > mid)
            {
                return false;
            }
            else
            {
                w = a[i];
                cnt++;
            }
        }
    }

    if (w > 0)
    {
        cnt++;
    }

    return cnt <= m;
}

