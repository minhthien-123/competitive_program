{
    std::ios_base::sync_with_stdio(0);
    std::cin.tie(0);
    std::cout.tie(0);

    if (std::fopen(task ".inp", "r"))
    {
        std::freopen(task ".inp", "r", stdin);
        std::freopen(task ".out", "w", stdout);
    }

    if (std::cin >> n >> m)
    {
        for (int i = 1; i <= n; i++)
        {
            for (int j = 1; j <= m; j++)
            {
                char ch;
                std::cin >> ch;
                if (ch == '.')
                {
                    a[i][j] = 0;
                }
                else if (ch == '#')
                {
                    a[i][j] = 1;
                }
                else if (ch == 'c')
                {
                    a[i][j] = 2;
                }
                else if (ch == 'f')
                {
                    a[i][j] = 3;
                }
            }
        }

        for (int i = 1; i <= n; i++)
        {
            for (int j = 1; j <= m; j++)
            {
                if (a[i][j] != 1 && !vis[i][j])
                {
                    c = 0, g = 0;
                    bfs({i, j});
                    if (c > g)
                    {
                        cntc += c;
                    }
                    else
                    {
                        cntg += g;
                    }
                }
            }
        }

        std::cout << cntg << " " << cntc << "\n";
    }

    std::cerr << "\nTime elapsed: " << TIME << " s.\n";

    return 0;
}
