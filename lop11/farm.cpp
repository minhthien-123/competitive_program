const int maxn = 1e3;
const ll inf = 1e18;
const int mod = 1e9 + 7;
const int inv = (mod + 1) / 2;
const int lg = 20;

ll add(ll x, ll y)
{
    return (x + y + 2 * mod) % mod;
}
ll sub(ll x, ll y)
{
    return (x - y + 2 * mod) % mod;
}
ll mul(ll x, ll y)
{
    return (x * y) % mod;
}
ll power(ll x, ll y)
{
    if (y == 0)
    {
        return 1;
    }
    ll tmp = power(x, y / 2);
    if (y % 2 == 0)
    {
        return (tmp * tmp) % mod;
    }
    else
    {
        return tmp * tmp % mod * x % mod;
    }
}

int n, m;
int a[maxn + 7][maxn + 7];
bool vis[maxn + 7][maxn + 7];
int dx[] = {-1, 0, 1, 0};
int dy[] = {0, -1, 0, 1};
int cntc = 0, cntg = 0;
int c = 0, g = 0;

bool valid(int x, int y)
{
    return (x >= 1 && x <= n && y >= 1 && y <= m && a[x][y] != 1 && !vis[x][y]);
}

void bfs(ii s)
{
    std::queue<ii> qu;
    qu.push(s);
    vis[s.fi][s.se] = true;

    while (qu.size())
    {
        ii t = qu.front();
        qu.pop();
        int x = t.fi, y = t.se;
        if (a[x][y] == 2)
        {
            c++;
        }
        else if (a[x][y] == 3)
        {
            g++;
        }

        for (int i = 0; i < 4; i++)
        {
            int nx = x + dx[i], ny = y + dy[i];
            if (valid(nx, ny))
            {
                vis[nx][ny] = true;
                qu.push({nx, ny});
            }
        }
    }
}

