ll add(ll x, ll y)
{
    return (x + y + 2 * mod) % mod;
}
ll sub(ll x, ll y)
{
    return (x - y + 2 * mod) % mod;
}
ll mul(ll x, ll y)
{
    return (x * y) % mod;
}
ll power(ll x, ll y)
{
    if (y == 0)
    {
        return 1;
    }
    ll tmp = power(x, y / 2);
    if (y % 2 == 0)
    {
        return (tmp * tmp) % mod;
    }
    else
    {
        return tmp * tmp % mod * x % mod;
    }
}

std::vector<int> adj[maxn + 7];
std::string s;
int mp[30];

bool check(int len)
{
    if (len < 26)
    {
        return false;
    }
    memset(mp, 0, sizeof mp);

    int cnt = 0;
    for (int i = 0; i < len; i++)
    {
        int idx = s[i] - 'A';
        if (idx < 0 || idx >= 26)
        {
            continue;
        }
        if (mp[idx] == 0)
        {
            cnt++;
        }
        mp[idx]++;
    }
    if (cnt == 26)
    {
        return true;
    }
    for (int i = len; i < sz(s); i++)
    {
        int l = s[i - len] - 'A';
        int r = s[i] - 'A';
        if (l >= 0 && l < 26)
        {
            mp[l]--;
            if (mp[l] == 0)
            {
                cnt--;
            }
        }
        if (r >= 0 && r < 26)
        {
            if (mp[r] == 0)
            {
                cnt++;
            }
            mp[r]++;
        }
        if (cnt == 26)
        {
            return true;
        }
    }
    return false;
}

