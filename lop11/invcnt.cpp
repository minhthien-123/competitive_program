ll add(ll x, ll y)
{
    return (x + y + 2 * mod) % mod;
}
ll sub(ll x, ll y)
{
    return (x - y + 2 * mod) % mod;
}
ll mul(ll x, ll y)
{
    return (x * y) % mod;
}
ll power(ll x, ll y)
{
    if (y == 0)
    {
        return 1;
    }
    ll tmp = power(x, y / 2);
    if (y % 2 == 0)
    {
        return (tmp * tmp) % mod;
    }
    else
    {
        return tmp * tmp % mod * x % mod;
    }
}

std::vector<int> adj[maxn + 7];
int n;
int a[maxn + 7], bit[maxn + 7];

int get(int id)
{
    int sum = 0;
    while (id > 0)
    {
        sum += bit[id];
        id -= id & (-id);
    }
    return sum;
}

void update(int id, int val)
{
    while (id <= n)
    {
        bit[id] += val;
        id += id & (-id);
    }
}

void convert(int arr[], int n)
{
    std::vector<int> tmp(n);
    for (int i = 0; i < n; i++)
        tmp[i] = arr[i + 1];
    std::sort(tmp.begin(), tmp.end());
    tmp.erase(std::unique(tmp.begin(), tmp.end()), tmp.end());
    for (int i = 1; i <= n; i++)
    {
        arr[i] = std::lower_bound(tmp.begin(), tmp.end(), arr[i]) - tmp.begin() + 1;
    }
}

