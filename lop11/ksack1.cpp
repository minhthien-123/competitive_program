const int maxn = 1e2;
const ll inf = 1e9;
const int mod = 1e9 + 7;
const int inv = (mod + 1) / 2;
const int lg = 20;

ll add(ll x, ll y)
{
    return (x + y + 2 * mod) % mod;
}
ll sub(ll x, ll y)
{
    return (x - y + 2 * mod) % mod;
}
ll mul(ll x, ll y)
{
    return (x * y) % mod;
}
ll power(ll x, ll y)
{
    if (y == 0)
    {
        return 1;
    }
    ll tmp = power(x, y / 2);
    if (y % 2 == 0)
    {
        return (tmp * tmp) % mod;
    }
    else
    {
        return tmp * tmp % mod * x % mod;
    }
}

std::vector<int> adj[maxn + 7];
int n, m, id;
ll pre[maxn + 7], res;
int p[maxn + 7], r[maxn + 7];
bool vis[maxn + 7];

struct pt
{
    int w, v, id;
    friend int operator<(pt a, pt b)
    {
        return a.v > b.v;
    }
};

pt a[maxn + 7];

void Try(int i = 1, int w = 0, ll sum = 0)
{
    if (i > n)
    {
        if (sum > res)
        {
            res = sum;
            id = 0;
            for (int j = 1; j <= n; j++)
            {
                if (vis[j])
                {
                    r[++id] = a[j].id;
                }
            }
        }
        return;
    }
    else
    {
        if (sum + pre[n] - pre[i - 1] <= res)
        {
            return;
        }

        Try(i + 1, w, sum);
        if (w + a[i].w <= m)
        {
            vis[i] = true;
            Try(i + 1, w + a[i].w, sum + a[i].v);
            vis[i] = false;
        }

        if (w + p[i] > m)
        {
            Try(n, w, sum);
        }
    }
}

