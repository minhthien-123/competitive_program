const int maxn = 1e3;
const int inf = 1e9;
const int mod = 1e9 + 7;
const int inv = (mod + 1) / 2;
const int lg = 20;

ll add(ll x, ll y)
{
    return (x + y + 2 * mod) % mod;
}
ll sub(ll x, ll y)
{
    return (x - y + 2 * mod) % mod;
}
ll mul(ll x, ll y)
{
    return (x * y) % mod;
}
ll power(ll x, ll y)
{
    if (y == 0)
    {
        return 1;
    }
    ll tmp = power(x, y / 2);
    if (y % 2 == 0)
    {
        return (tmp * tmp) % mod;
    }
    else
    {
        return tmp * tmp % mod * x % mod;
    }
}

int n, m;
int a[maxn + 7][maxn + 7];
bool vis[maxn + 7][maxn + 7];
int dist[maxn + 7][maxn + 7];
ii p;
ii trace[maxn + 7][maxn + 7];
int res = inf;
ii t = {0, 0};
int dx[] = {-1, 1, 0, 0};
int dy[] = {0, 0, -1, 1};

void bfs(ii s)
{
    if (a[s.fi][s.se] != 0)
        return;

    std::queue<ii> qu;
    qu.push(s);
    vis[s.fi][s.se] = true;
    dist[s.fi][s.se] = 1;
    trace[s.fi][s.se] = {-1, -1};

    if (s.fi == 1 || s.fi == m || s.se == 1 || s.se == n)
    {
        res = 1;
        t = s;
        return;
    }

    while (sz(qu))
    {
        auto [x, y] = qu.front();
        qu.pop();

        for (int i = 0; i < 4; i++)
        {
            int nx = x + dx[i];
            int ny = y + dy[i];

            if (nx >= 1 && nx <= m && ny >= 1 && ny <= n && !vis[nx][ny] && a[nx][ny] == 0)
            {
                vis[nx][ny] = true;
                dist[nx][ny] = dist[x][y] + 1;
                trace[nx][ny] = {x, y};
                qu.push({nx, ny});

                if (nx == 1 || nx == m || ny == 1 || ny == n)
                {
                    res = dist[nx][ny];
                    t = {nx, ny};
                    return;
                }
            }
        }
    }
}

