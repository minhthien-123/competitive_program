int n, m, q;
int x, y;
int a[maxn + 7][maxn + 7];
bool vis[maxn + 7][maxn + 7], ans[maxn + 7][maxn + 7];
ii p;
int dx[] = {0, -1, 1, 0};
int dy[] = {-1, 0, 0, 1};

bool valid(int x, int y)
{
    if (vis[x][y] || x < 1 || y < 1 || x > m || y > n)
    {
        return false;
    }
    return true;
}

void bfs(ii s)
{
    std::queue<ii> qu;
    qu.push(s);
    vis[s.fi][s.se] = ans[s.fi][s.se] = true;

    while (sz(qu))
    {
        auto [x, y] = qu.front();
        qu.pop();

        if (a[x][y] != 0)
        {
            continue;
        }

        for (int i = 0; i < 4; i++)
        {
            int nx = x + dx[i];
            int ny = y + dy[i];
            if (valid(nx, ny))
            {
                qu.push({nx, ny});
                vis[nx][ny] = true;
                ans[nx][ny] = a[nx][ny] == 0;
            }
        }
    }
}

