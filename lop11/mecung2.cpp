const int maxn = 1e3;
const int inf = 1e9;
const int mod = 1e9 + 7;
const int inv = (mod + 1) / 2;
const int lg = 20;

ll add(ll x, ll y)
{
    return (x + y + 2 * mod) % mod;
}
ll sub(ll x, ll y)
{
    return (x - y + 2 * mod) % mod;
}
ll mul(ll x, ll y)
{
    return (x * y) % mod;
}
ll power(ll x, ll y)
{
    if (y == 0)
    {
        return 1;
    }
    ll tmp = power(x, y / 2);
    if (y % 2 == 0)
    {
        return (tmp * tmp) % mod;
    }
    else
    {
        return tmp * tmp % mod * x % mod;
    }
}

std::vector<int> adj[maxn + 7];
int n, m, q;
ii s;
int a[maxn + 7][maxn + 7], dist[maxn + 7][maxn + 7];
int dx[8] = {-1, 1, 0, 0, -1, -1, 1, 1};
int dy[8] = {0, 0, -1, 1, -1, 1, -1, 1};

bool valid(int x, int y)
{
    return (x >= 1 && x <= m && y >= 1 && y <= n);
}

void bfs(ii s)
{
    std::queue<ii> qu;
    qu.push(s);
    dist[s.fi][s.se] = 0;

    while (sz(qu))
    {
        auto [x, y] = qu.front();
        qu.pop();

        for (int i = 0; i < 4; i++)
        {
            int nx = x + dx[i];
            int ny = y + dy[i];

            if (!valid(nx, ny) || a[nx][ny] == 1)
            {
                continue;
            }

            if (dist[nx][ny] > dist[x][y] + 1)
            {
                dist[nx][ny] = dist[x][y] + 1;
                qu.push({nx, ny});
            }
        }
    }
}

