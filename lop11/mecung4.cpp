const int maxn = 1e3;
const int inf = 1e9;
const int mod = 1e9 + 7;
const int inv = (mod + 1) / 2;
const int lg = 20;
const int dx[] = {-1, 1, 0, 0, -1, -1, 1, 1};
const int dy[] = {0, 0, -1, 1, -1, 1, -1, 1};

ll add(ll x, ll y)
{
    return (x + y + 2 * mod) % mod;
}
ll sub(ll x, ll y)
{
    return (x - y + 2 * mod) % mod;
}
ll mul(ll x, ll y)
{
    return (x * y) % mod;
}
ll power(ll x, ll y)
{
    if (y == 0)
    {
        return 1;
    }
    ll tmp = power(x, y / 2);
    if (y % 2 == 0)
    {
        return (tmp * tmp) % mod;
    }
    else
    {
        return tmp * tmp % mod * x % mod;
    }
}

std::vector<int> adj[maxn + 7];
int n, m;
int a[maxn + 7][maxn + 7];
int dist[maxn + 7][maxn + 7];
bool vis[maxn + 7][maxn + 7];
std::deque<ii> dq;

bool valid(int x, int y)
{
    return (x >= 1 && x <= m && y >= 1 && y <= n);
}

void bfs()
{
    while (sz(dq))
    {
        auto [x, y] = dq.front();
        dq.pop_front();

        if (vis[x][y])
        {
            continue;
        }
        vis[x][y] = true;

        for (int i = 0; i < 4; i++)
        {
            int nx = x + dx[i];
            int ny = y + dy[i];

            if (!valid(nx, ny))
            {
                continue;
            }

            int cost = (a[x][y] != a[nx][ny]);
            if (dist[nx][ny] > dist[x][y] + cost)
            {
                dist[nx][ny] = dist[x][y] + cost;
                if (cost == 0)
                {
                    dq.push_front({nx, ny});
                }
                else
                {
                    dq.push_back({nx, ny});
                }
            }
        }
    }
}

