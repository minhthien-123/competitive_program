bool isprime[maxn + 7];

void sieve()
{
    for (int i = 1; i <= maxn; i++)
    {
        isprime[i] = 1;
    }
    isprime[1] = 0;
    for (int i = 2; i <= maxn; i++)
    {
        if (isprime[i])
        {
            for (int j = 2 * i; j <= maxn; j += i)
            {
                isprime[j] = 0;
            }
        }
    }
}

