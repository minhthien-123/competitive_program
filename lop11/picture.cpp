const int maxn = 1e2;
const ll inf = 1e18;
const int mod = 1e9 + 7;
const int inv = (mod + 1) / 2;
const int lg = 20;

ll add(ll x, ll y)
{
    return (x + y + 2 * mod) % mod;
}
ll sub(ll x, ll y)
{
    return (x - y + 2 * mod) % mod;
}
ll mul(ll x, ll y)
{
    return (x * y) % mod;
}
ll power(ll x, ll y)
{
    if (y == 0)
    {
        return 1;
    }
    ll tmp = power(x, y / 2);
    if (y % 2 == 0)
    {
        return (tmp * tmp) % mod;
    }
    else
    {
        return tmp * tmp % mod * x % mod;
    }
}

std::vector<int> adj[maxn + 7];
int n, m;
int a[maxn + 7][maxn + 7];
bool vis[maxn + 7][maxn + 7];
int dx[8] = {-1, 1, 0, 0, -1, -1, 1, 1};
int dy[8] = {0, 0, -1, 1, -1, 1, -1, 1};
bool flag;

bool valid(int x, int y)
{
    return (x >= 1 && x <= m && y >= 1 && y <= n);
}

void bfs(ii s)
{
    std::queue<ii> qu;
    qu.push(s);
    vis[s.fi][s.se] = true;

    while (sz(qu))
    {
        auto [x, y] = qu.front();
        qu.pop();

        for (int i = 0; i < 8; i++)
        {
            int nx = x + dx[i];
            int ny = y + dy[i];

            if (!valid(nx, ny))
            {
                continue;
            }

            if (a[nx][ny] > a[x][y])
            {
                flag = false;
            }
            else if (a[nx][ny] == a[x][y])
            {
                if (!vis[nx][ny])
                {
                    vis[nx][ny] = true;
                    qu.push({nx, ny});
                }
            }
        }
    }
}

