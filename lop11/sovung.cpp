{
    std::ios_base::sync_with_stdio(0);
    std::cin.tie(0);
    std::cout.tie(0);

    if (std::fopen(task ".inp", "r"))
    {
        std::freopen(task ".inp", "r", stdin);
        std::freopen(task ".out", "w", stdout);
    }

    std::cin >> m >> n >> k;

    while (k--)
    {
        int x, y, z, t;
        std::cin >> x >> y >> z >> t;
        int x1 = std::min(x, z);
        int x2 = std::max(x, z);
        int y1 = std::min(y, t);
        int y2 = std::max(y, t);

        a[x1][y1]++;
        a[x2 + 1][y1]--;
        a[x1][y2 + 1]--;
        a[x2 + 1][y2 + 1]++;
    }

    for (int i = 1; i <= m; i++)
    {
        for (int j = 1; j <= n; j++)
        {
            a[i][j] += a[i - 1][j] + a[i][j - 1] - a[i - 1][j - 1];
        }
    }

    int ans = 0;
    for (int i = 1; i <= m; i++)
    {
        for (int j = 1; j <= n; j++)
        {
            if (!vis[i][j] && a[i][j] == 0)
            {
                ans++;
                bfs({i, j});
            }
        }
    }

    std::cout << ans;

    std::cerr << "\nTime elapsed: " << TIME << " s.\n";

    return 0;
}
