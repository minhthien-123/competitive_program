ll add(ll x, ll y)
{
    return (x + y + 2 * mod) % mod;
}
ll sub(ll x, ll y)
{
    return (x - y + 2 * mod) % mod;
}
ll mul(ll x, ll y)
{
    return (x * y) % mod;
}
ll power(ll x, ll y)
{
    if (y == 0)
    {
        return 1;
    }
    ll tmp = power(x, y / 2);
    if (y % 2 == 0)
    {
        return (tmp * tmp) % mod;
    }
    else
    {
        return tmp * tmp % mod * x % mod;
    }
}

std::vector<int> adj[maxn + 7];
int n;
ll m;
ll a[maxn + 7], pre[maxn + 7];
ll res = 0;
std::vector<int> v, ans;

void Try(int i = 1, int sum = 0)
{
    if (res == m - 1)
    {
        return;
    }
    if (i > n)
    {
        if (sum > res)
        {
            res = sum;
            ans = v;
        }
        return;
    }
    Try(i + 1, sum);
    if (sum + a[i] < m && sum + pre[n] - pre[i - 1] > res)
    {
        v.pb(i);
        Try(i + 1, sum + a[i]);
        v.pop_back();
    }
}

