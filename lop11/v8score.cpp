ll add(ll x, ll y)
{
    return (x + y + 2 * mod) % mod;
}
ll sub(ll x, ll y)
{
    return (x - y + 2 * mod) % mod;
}
ll mul(ll x, ll y)
{
    return (x * y) % mod;
}
ll power(ll x, ll y)
{
    if (y == 0)
    {
        return 1;
    }
    ll tmp = power(x, y / 2);
    if (y % 2 == 0)
    {
        return (tmp * tmp) % mod;
    }
    else
    {
        return tmp * tmp % mod * x % mod;
    }
}

std::vector<int> adj[maxn + 7];
int s, n, k;
int a[maxn + 7][maxn + 7];
int f[maxn + 7];

void solve(int pos, int sum)
{
    for (int i = 1; i <= n; i++)
    {
        if (a[i][pos] < f[pos - 1])
        {
            break;
        }
        f[pos] = a[i][pos];
        sum = sum + a[i][pos];
        if (s - sum >= f[pos] * (k - pos))
        {
            if (pos == k)
            {
                if (sum != s)
                {
                    continue;
                }
                std::cout << "YES\n";
                for (int i = 1; i <= k; i++)
                {
                    std::cout << f[i] << " ";
                }
                exit(0);
            }
            else
            {
                solve(pos + 1, sum);
            }
        }
        sum = sum - a[i][pos];
    }
}

