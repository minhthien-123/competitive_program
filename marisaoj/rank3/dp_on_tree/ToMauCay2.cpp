const int maxn = 1e5;
const ll inf = 1e18;
const int mod = 1e9 + 7;
const int inv = (mod + 1) / 2;
const int lg = 20;

ll add(ll x, ll y)
{
    return (x + y + 2 * mod) % mod;
}
ll sub(ll x, ll y)
{
    return (x - y + 2 * mod) % mod;
}
ll mul(ll x, ll y)
{
    return (x * y) % mod;
}
ll power(ll x, ll y)
{
    if (y == 0)
    {
        return 1;
    }
    ll tmp = power(x, y / 2);
    if (y % 2 == 0)
    {
        return (tmp * tmp) % mod;
    }
    else
    {
        return tmp * tmp % mod * x % mod;
    }
}

std::vector<int> adj[maxn + 7];
int n;
ll dp[maxn + 7][5];

void calc(int u, int p)
{
    dp[u][1] = dp[u][2] = dp[u][3] = 1;

    for (int v : adj[u])
    {
        if (v == p)
            continue;

        calc(v, u);

        dp[u][1] = mul(dp[u][1], add(dp[v][2], dp[v][3]));
        dp[u][2] = mul(dp[u][2], add(dp[v][1], dp[v][3]));
        dp[u][3] = mul(dp[u][3], add(dp[v][1], dp[v][2]));
    }
}

