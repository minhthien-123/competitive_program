const int maxn = 2e5;
const ll inf = 1e18;
const int mod = 998244353;
const int inv = (mod + 1) / 2;
const int lg = 20;

ll add(ll x, ll y)
{
    return (x + y + 2 * mod) % mod;
}
ll sub(ll x, ll y)
{
    return (x - y + 2 * mod) % mod;
}
ll mul(ll x, ll y)
{
    return (x * y) % mod;
}
ll power(ll x, ll y)
{
    if (y == 0)
    {
        return 1;
    }
    ll tmp = power(x, y / 2);
    if (y % 2 == 0)
    {
        return (tmp * tmp) % mod;
    }
    else
    {
        return tmp * tmp % mod * x % mod;
    }
}

int n, q;
ll p[maxn + 7], a[maxn + 7];

struct Seg
{
    ll val;
    int lazy;
} st[4 * maxn + 7];

void build(int id, int l, int r)
{
    st[id].lazy = -1;
    if (l == r)
    {
        st[id].val = 1;
        return;
    }
    int mid = (l + r) / 2;
    build(id * 2, l, mid);
    build(id * 2 + 1, mid + 1, r);
    st[id].val = (st[id * 2].val * p[r - mid] + st[id * 2 + 1].val) % mod;
}

void upd(int id, int l, int r, int d)
{
    int len = r - l + 1;
    st[id].val = (a[len] * d) % mod;
    st[id].lazy = d;
}

void fixed(int id, int l, int r)
{
    if (st[id].lazy != -1 && l != r)
    {
        int mid = (l + r) / 2;
        upd(id * 2, l, mid, st[id].lazy);
        upd(id * 2 + 1, mid + 1, r, st[id].lazy);
        st[id].lazy = -1;
    }
}

void update(int id, int l, int r, int u, int v, int d)
{
    if (u > r || v < l)
        return;
    if (u <= l && r <= v)
    {
        upd(id, l, r, d);
        return;
    }
    fixed(id, l, r);
    int mid = (l + r) / 2;
    update(id * 2, l, mid, u, v, d);
    update(id * 2 + 1, mid + 1, r, u, v, d);
    st[id].val = (st[id * 2].val * p[r - mid] + st[id * 2 + 1].val) % mod;
}

