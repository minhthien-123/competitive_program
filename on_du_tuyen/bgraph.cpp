const int maxn = 300000;
const ll inf = 1e18;
const int mod = 998244353;
const int inv = (mod + 1) / 2;
const int lg = 20;

ll add(ll x, ll y)
{
    return (x + y + 2 * mod) % mod;
}
ll sub(ll x, ll y)
{
    return (x - y + 2 * mod) % mod;
}
ll mul(ll x, ll y)
{
    return (x * y) % mod;
}
ll power(ll x, ll y)
{
    if (y == 0)
    {
        return 1;
    }
    ll tmp = power(x, y / 2);
    if (y % 2 == 0)
    {
        return (tmp * tmp) % mod;
    }
    else
    {
        return tmp * tmp % mod * x % mod;
    }
}

std::vector<int> adj[maxn + 7];
int n, m;
int dp[maxn + 7][5];
int col[maxn + 7];
ll pow2[maxn + 7];

ll bfs(int s)
{
    std::queue<int> q;
    q.push(s);
    col[s] = 0;
    int cnt0 = 0, cnt1 = 0;
    while (!q.empty())
    {
        int u = q.front();
        q.pop();
        if (col[u] == 0)
        {
            cnt0++;
        }
        else
        {
            cnt1++;
        }
        for (int v : adj[u])
        {
            if (col[v] == -1)
            {
                col[v] = col[u] ^ 1;
                q.push(v);
            }
            else
            {
                if (col[v] == col[u])
                {
                    std::cout << 0;
                    exit(0);
                }
            }
        }
    }
    return add(pow2[cnt0], pow2[cnt1]);
}

