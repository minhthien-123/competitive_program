ll add(ll x, ll y)
{
    return (x + y + 2 * mod) % mod;
}
ll sub(ll x, ll y)
{
    return (x - y + 2 * mod) % mod;
}
ll mul(ll x, ll y)
{
    return (x * y) % mod;
}
ll power(ll x, ll y)
{
    if (y == 0)
    {
        return 1;
    }
    ll tmp = power(x, y / 2);
    if (y % 2 == 0)
    {
        return (tmp * tmp) % mod;
    }
    else
    {
        return tmp * tmp % mod * x % mod;
    }
}

std::vector<int> adj[maxn + 7];
int n, k;
ll a[maxn + 7], pre[maxn + 7];

void sub1()
{
    ld ans = 0;
    for (int i = 1; i <= n; i++)
    {
        pre[i] = pre[i - 1] + a[i];
    }
    for (int i = 1; i + k - 1 <= n; i++)
    {
        for (int j = i + k - 1; j <= n; j++)
        {
            //std::cout << i << " " << j << "\n";
            ld cnt = j - i + 1;
            ld res = (pre[j] - pre[i - 1]) / cnt;
            ans = std::max(ans, res);
        }
    }

    std::cout << std::setprecision(6) << std::fixed  << ans;
}

