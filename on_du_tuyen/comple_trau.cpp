const int maxn = 2e3;
const ll inf = 1e18;
const int mod = 1e9 + 7;
const int inv = (mod + 1) / 2;
const int lg = 20;

ll add(ll x, ll y)
{
    return (x + y + 2 * mod) % mod;
}
ll sub(ll x, ll y)
{
    return (x - y + 2 * mod) % mod;
}
ll mul(ll x, ll y)
{
    return (x * y) % mod;
}
ll power(ll x, ll y)
{
    if (y == 0)
    {
        return 1;
    }
    ll tmp = power(x, y / 2);
    if (y % 2 == 0)
    {
        return (tmp * tmp) % mod;
    }
    else
    {
        return tmp * tmp % mod * x % mod;
    }
}

std::vector<int> adj[maxn + 7], in[maxn + 7], out[maxn + 7];
int n;
char a[maxn + 7][maxn + 7];
int par[maxn + 7];
bool vis[maxn + 7];
bool check = false;
int s;

void dfs(int u, int d)
{
    if (check)
    {
        return;
    }
    if (d == 2)
    {
        if (a[u][s] == '1')
        {
            check = true;
        }
        return;
    }
    for (int v : adj[u])
    {
        if (v == s)
        {
            continue;
        }
        if (!vis[v])
        {
            vis[v] = true;
            dfs(v, d + 1);
            vis[v] = false;
            if (check)
            {
                return;
            }
        }
    }
}

void sub1()
{
    for (int i = 1; i <= n && !check; i++)
    {
        s = i;
        memset(vis, 0, sizeof(vis));
        vis[s] = true;
        for (int v : adj[s])
        {
            if (v == s)
            {
                continue;
            }
            vis[v] = true;
            dfs(v, 1);
            vis[v] = false;
            if (check)
            {
                break;
            }
        }
    }

    if (check)
        std::cout << 3 << '\n';
    else
        std::cout << 0 << '\n';
}

