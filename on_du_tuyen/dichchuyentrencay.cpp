const int maxn = 200000;
const int inf = 1e9;
const int mod = 1e9 + 7;
const int inv = (mod + 1) / 2;
const int lg = 20;

ll add(ll x, ll y)
{
    return (x + y + 2 * mod) % mod;
}
ll sub(ll x, ll y)
{
    return (x - y + 2 * mod) % mod;
}
ll mul(ll x, ll y)
{
    return (x * y) % mod;
}
ll power(ll x, ll y)
{
    if (y == 0)
    {
        return 1;
    }
    ll tmp = power(x, y / 2);
    if (y % 2 == 0)
    {
        return (tmp * tmp) % mod;
    }
    else
    {
        return tmp * tmp % mod * x % mod;
    }
}

std::vector<ii> adj[maxn + 7];
int n, q;
std::string s;
int up[maxn + 7][lg], h[maxn + 7];
// sửa d: d[s 1..26][node 1..n+26]
int d[29][maxn + 30];

void dfs(int u)
{
    for (auto tmp : adj[u])
    {
        int v = tmp.fi;
        int w = tmp.se;
        if (up[u][0] == v)
        {
            continue;
        }

        up[v][0] = u;
        h[v] = h[u] + 1;

        for (int j = 1; j < lg; j++)
        {
            up[v][j] = up[up[v][j - 1]][j - 1];
        }

        dfs(v);
    }
}

int lca(int u, int v)
{
    if (!u || !v)
    {
        return u + v;
    }

    if (h[u] != h[v])
    {
        if (h[u] < h[v])
        {
            std::swap(u, v);
        }
        int k = h[u] - h[v];

        for (int j = 0; j < lg; j++)
        {
            if (k >> j & 1)
            {
                u = up[u][j];
            }
        }
    }

    if (u == v)
    {
        return u;
    }

    for (int j = lg - 1; j >= 0; j--)
    {
        if (up[u][j] != up[v][j])
        {
            u = up[u][j];
            v = up[v][j];
        }
    }

    return up[u][0];
}

void dijkstra(int s)
{
    for (int i = 1; i <= n + 26; i++)
    {
        d[s][i] = inf;
    }

    d[s][s + n] = 0;
    std::priority_queue<ii, std::vector<ii>, std::greater<ii>> pq;
    pq.push({0, s + n});
    while (!pq.empty())
    {
        auto tmp = pq.top();
        pq.pop();
        int du = tmp.fi;
        int u = tmp.se;
        if (d[s][u] != du)
        {
            continue;
        }

        for (auto e : adj[u])
        {
            int v = e.fi;
            int w = e.se;

            if (d[s][v] > d[s][u] + w)
            {
                d[s][v] = d[s][u] + w;
                pq.push({d[s][v], v});
            }
        }
    }
}

