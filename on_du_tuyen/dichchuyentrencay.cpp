{
    std::ios_base::sync_with_stdio(0);
    std::cin.tie(0);
    std::cout.tie(0);

    if (std::fopen(task ".inp", "r"))
    {
        std::freopen(task ".inp", "r", stdin);
        std::freopen(task ".out", "w", stdout);
    }

    std::cin >> n;
    std::cin >> s;
    for (int i = 1; i < n; i++)
    {
        int u, v;
        std::cin >> u >> v;
        adj[u].pb({v, 2});
        adj[v].pb({u, 2});
    }

    dfs(1);

    int m = sz(s);
    s = " " + s;
    for (int i = 1; i <= m; i++)
    {
        adj[s[i] - 'a' + n + 1].pb({i, 1});
        adj[i].pb({s[i] - 'a' + n + 1, 1});
    }

    for (int i = 1; i <= 26; i++)
    {
        dijkstra(i);
    }

    std::cin >> q;
    for (int i = 1; i <= q; i++)
    {
        int u, v;
        std::cin >> u >> v;
        int res = h[u] + h[v] - 2 * h[lca(u, v)];
        res *= 2;
        for (int j = 1; j <= 26; j++)
        {
            res = std::min(res, d[j][u] + d[j][v]);
        }
        std::cout << res / 2 << '\n';
    }

    std::cerr << "\nTime elapsed: " << TIME << " s.\n";

    return 0;
}

