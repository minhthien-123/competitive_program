ll add(ll x, ll y)
{
    return (x + y + 2 * mod) % mod;
}
ll sub(ll x, ll y)
{
    return (x - y + 2 * mod) % mod;
}
ll mul(ll x, ll y)
{
    return (x * y) % mod;
}
ll power(ll x, ll y)
{
    if (y == 0)
    {
        return 1;
    }
    ll tmp = power(x, y / 2);
    if (y % 2 == 0)
    {
        return (tmp * tmp) % mod;
    }
    else
    {
        return tmp * tmp % mod * x % mod;
    }
}

const int test = 100;

std::mt19937_64 rd(std::chrono::steady_clock::now().time_since_epoch().count());

int Rand(int l, int r)
{
    if (l > r)
    {
        std::swap(l, r);
    }
    std::uniform_int_distribution<int> dist(l, r);
    return dist(rd);
}

