struct DSU
{
    std::vector<int> par, sz;

    DSU(int n)
    {
        par.resize(n + 7);
        sz.resize(n + 7);

        for (int i = 0; i <= n + 1; i++)
        {
            par[i] = i;
            a[i] = 1;
            sz[i] = 1;
        }
    }

    int acs(int v)
    {
        return v == par[v] ? v : par[v] = acs(par[v]);
    }

    void join(int a, int b)
    {
        a = acs(a);
        b = acs(b);
        if (a != b)
        {
            if (sz[a] < sz[b])
            {
                std::swap(a, b);
            }
            par[b] = a;
            sz[a] += sz[b];
        }
    }
};

