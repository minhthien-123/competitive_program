ll ans = 0;

struct DSU
{
    std::vector<int> par, sz, val;

    DSU(int n)
    {
        par.resize(n + 7);
        sz.resize(n + 7);
        val.resize(n + 7);

        for (int i = 0; i <= n + 1; i++)
        {
            par[i] = i;
            sz[i] = 1;
            val[i] = a[i];
        }
    }

    int acs(int v)
    {
        return v == par[v] ? v : par[v] = acs(par[v]);
    }

    void join(int a, int b)
    {
        a = acs(a);
        b = acs(b);
        if (a != b)
        {
            ans += val[a] + val[b];
            if (sz[a] < sz[b])
            {
                std::swap(a, b);
            }
            par[b] = a;
            sz[a] += sz[b];
            val[a] = std::max(val[a], val[b]);

        }
    }
};

struct Edge
{
    ll w;
    int u, v;
    friend bool operator<(Edge x, Edge y)
    {
        return x.w < y.w;
    }
};

