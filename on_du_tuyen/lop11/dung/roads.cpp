ll add(ll x, ll y)
{
    return (x + y + 2 * mod) % mod;
}
ll sub(ll x, ll y)
{
    return (x - y + 2 * mod) % mod;
}
ll mul(ll x, ll y)
{
    return (x * y) % mod;
}
ll power(ll x, ll y)
{
    if (y == 0)
    {
        return 1;
    }
    ll tmp = power(x, y / 2);
    if (y % 2 == 0)
    {
        return (tmp * tmp) % mod;
    }
    else
    {
        return tmp * tmp % mod * x % mod;
    }
}

int n, k, m;

struct State
{
    long long d;
    int u;
    int cost;
    bool operator>(const State &other) const
    {
        return d > other.d;
    }
};

struct Dijkstra
{
    int n, k;
    std::vector<std::vector<std::pair<int, std::pair<long long, int>>>> adj;
    std::vector<std::vector<long long>> dist;
    std::vector<int> min_cost;
    const long long INF = 1e18;

    Dijkstra(int n, int k)
    {
        this->n = n;
        this->k = k;
        adj.resize(n + 7);
        dist.assign(n + 7, std::vector<long long>(k + 7, INF));
        min_cost.assign(n + 7, 2e9);
    }

    void add_edge(int u, int v, long long l, int t)
    {
        adj[u].push_back({v, {l, t}});
    }

    long long run(int s, int target)
    {
        std::priority_queue<State, std::vector<State>, std::greater<State>> pq;

        dist[s][0] = 0;
        pq.push({0, s, 0});

        while (!pq.empty())
        {
            State top = pq.top();
            pq.pop();

            long long d = top.d;
            int u = top.u;
            int cost = top.cost;

            if (d > dist[u][cost])
                continue;

            if (u == target)
                return d;

            if (cost >= min_cost[u])
                continue;

            min_cost[u] = cost;

            for (auto edge : adj[u])
            {
                int v = edge.first;
                long long l = edge.second.first;
                int t = edge.second.second;

                if (cost + t <= k)
                {
                    if (d + l < dist[v][cost + t])
                    {
                        dist[v][cost + t] = d + l;
                        pq.push({d + l, v, cost + t});
                    }
                }
            }
        }
        return INF;
    }
};

