const int maxn = 205;
const ll inf = 1e18;
const int mod = 1e9 + 7;
const int inv = (mod + 1) / 2;
const int lg = 20;

ll add(ll x, ll y) { return (x + y + 2 * mod) % mod; }
ll sub(ll x, ll y) { return (x - y + 2 * mod) % mod; }
ll mul(ll x, ll y) { return (x * y) % mod; }
ll power(ll x, ll y)
{
    if (y == 0)
        return 1;
    ll tmp = power(x, y / 2);
    if (y % 2 == 0)
        return (tmp * tmp) % mod;
    else
        return tmp * tmp % mod * x % mod;
}

int t;
int g[maxn + 7];

void init()
{
    g[0] = 0;
    g[1] = 0;
    g[2] = 0;
    for (int i = 3; i <= 200; i++)
    {
        std::vector<int> mex_set;
        for (int l = 0; l <= i - 2; l++)
        {
            int r = i - 2 - l;
            mex_set.pb(g[l] ^ g[r]);
        }
        std::sort(all(mex_set));
        mex_set.erase(std::unique(all(mex_set)), mex_set.end());
        int mex = 0;
        for (int x : mex_set)
        {
            if (x == mex)
                mex++;
            else
                break;
        }
        g[i] = mex;
    }
}

