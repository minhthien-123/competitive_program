ll add(ll x, ll y)
{
    return (x + y + 2 * mod) % mod;
}
ll sub(ll x, ll y)
{
    return (x - y + 2 * mod) % mod;
}
ll mul(ll x, ll y)
{
    return (x * y) % mod;
}
ll power(ll x, ll y)
{
    if (y == 0)
    {
        return 1;
    }
    ll tmp = power(x, y / 2);
    if (y % 2 == 0)
    {
        return (tmp * tmp) % mod;
    }
    else
    {
        return tmp * tmp % mod * x % mod;
    }
}

std::vector<int> adj[maxn + 7];
int n, q;
int a[maxn + 7], par[maxn + 7], sz[maxn + 7], nxt[maxn + 7];

void init(int v)
{
    par[v] = v;
    sz[v] = 1;
    nxt[v] = v;
}

int acs(int v)
{
    if (v == par[v])
    {
        return v;
    }
    return par[v] = acs(par[v]);
}

void join(int a, int b)
{
    a = acs(a);
    b = acs(b);
    if (a != b)
    {
        if (sz[a] < sz[b])
        {
            std::swap(a, b);
        }
        par[b] = a;
        sz[a] += sz[b];
    }
}

int find_nxt(int v)
{
    if (v == nxt[v])
    {
        return v;
    }
    return nxt[v] = find_nxt(nxt[v]);
}

