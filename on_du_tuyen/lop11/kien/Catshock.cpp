ll add(ll x, ll y)
{
    return (x + y + 2 * mod) % mod;
}
ll sub(ll x, ll y)
{
    return (x - y + 2 * mod) % mod;
}
ll mul(ll x, ll y)
{
    return (x * y) % mod;
}
ll power(ll x, ll y)
{
    if (y == 0)
    {
        return 1;
    }
    ll tmp = power(x, y / 2);
    if (y % 2 == 0)
    {
        return (tmp * tmp) % mod;
    }
    else
    {
        return tmp * tmp % mod * x % mod;
    }
}

std::vector<int> adj[maxn + 7];
int n;
int d[maxn + 7];
std::vector<int> vec;

void dfs(int u, int p)
{
    for (int v : adj[u])
    {
        if (v != p)
        {
            d[v] = d[u] + 1;
            dfs(v, u);
        }
    }
    if (u != n)
    {
        vec.pb(u);
    }
}

