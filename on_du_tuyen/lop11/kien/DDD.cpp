    int t;
    std::cin >> t;
    while (t--)
    {
        std::cin >> n >> m >> l;
        for (int i = 1; i <= n; i++)
        {
            dist[i][0] = dist[i][1] = inf;
            adj[i].clear();
        }
        ll e = -1, o = -1, s = 0, mn = inf;
        for (int i = 1; i <= l; i++)
        {
            std::cin >> a[i];
            s += a[i];
            if (a[i] % 2 != 0)
            {
                mn = std::min(mn, a[i]);
            }
        }

        if (s % 2 == 0)
        {
            e = s;
            if (mn != inf)
            {
                o = s - mn;
            }
        }
        else
        {
            o = s;
            e = s - mn;
        }

        //std::cout << s << " " << e << " " << o << "\n";

        while (m--)
        {
            int u, v;
            std::cin >> u >> v;
            adj[u].push_back(v);
            adj[v].push_back(u);
        }

        bfs(1);

        for (int i = 1; i <= n; i++)
        {
            if (dist[i][0] <= e || dist[i][1] <= o)
            {
                std::cout << 1;
            }
            else
            {
                std::cout << 0;
            }
        }

        std::cout << "\n";
    }

    std::cerr << "\nTime elapsed: " << TIME << " s.\n";

    return 0;
