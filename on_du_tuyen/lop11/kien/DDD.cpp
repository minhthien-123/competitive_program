const int maxn = 2e5;
const ll inf = 1e18;
const int mod = 1e9 + 7;
const int inv = (mod + 1) / 2;
const int lg = 20;
const int dx[] = {0, 1, 0, -1, -1, -1, 1, 1};
const int dy[] = {1, 0, -1, 0, -1, 1, -1, 1};

ll add(ll x, ll y)
{
    return (x + y + 2 * mod) % mod;
}
ll sub(ll x, ll y)
{
    return (x - y + 2 * mod) % mod;
}
ll mul(ll x, ll y)
{
    return (x * y) % mod;
}
ll power(ll x, ll y)
{
    if (y == 0)
    {
        return 1;
    }
    ll tmp = power(x, y / 2);
    if (y % 2 == 0)
    {
        return (tmp * tmp) % mod;
    }
    else
    {
        return tmp * tmp % mod * x % mod;
    }
}

std::vector<int> adj[maxn + 7];
int n, m, l;
ll a[maxn + 7], dist[maxn + 7][2];

void bfs(int s)
{
    std::queue<ii> qu;
    qu.push({s, 0});
    dist[s][0] = 0;

    while (sz(qu))
    {
        auto [u, w] = qu.front();
        qu.pop();

        for (int v : adj[u])
        {
            if (dist[v][1 - w] == inf)
            {
                dist[v][1 - w] = dist[u][w] + 1;
                qu.push({v, 1 - w});
            }
        }
    }
}

