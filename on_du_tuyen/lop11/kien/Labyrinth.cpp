const int maxn = 2e5;
const ll inf = 1e18;
const int mod = 1e9 + 7;
const int inv = (mod + 1) / 2;
const int lg = 20;
const int dx[] = {0, 1, 0, -1, -1, -1, 1, 1};
const int dy[] = {1, 0, -1, 0, -1, 1, -1, 1};

ll add(ll x, ll y) { return (x + y + 2 * mod) % mod; }
ll sub(ll x, ll y) { return (x - y + 2 * mod) % mod; }
ll mul(ll x, ll y) { return (x * y) % mod; }
ll power(ll x, ll y)
{
    if (y == 0)
        return 1;
    ll tmp = power(x, y / 2);
    if (y % 2 == 0)
        return (tmp * tmp) % mod;
    else
        return tmp * tmp % mod * x % mod;
}

std::vector<int> adj[maxn + 7];
int n, m, s;
int f[maxn + 7], par[maxn + 7];

void dfs(int u, int p, int r)
{
    par[u] = p;
    f[u] = r;
    for (int v : adj[u])
    {
        if (v == s)
        {
            continue;
        }

        if (!f[v])
        {
            dfs(v, u, r);
        }
        else if (f[v] != r)
        {
            std::vector<int> p1, p2;

            int tmp = u;
            while (tmp != s)
            {
                p1.pb(tmp);
                tmp = par[tmp];
            }
            p1.pb(s);
            std::reverse(all(p1));
            p1.pb(v);

            tmp = v;
            while (tmp != s)
            {
                p2.pb(tmp);
                tmp = par[tmp];
            }
            p2.pb(s);
            std::reverse(all(p2));

            std::cout << "Possible\n"
                      << sz(p1) << "\n";
            for (int x : p1)
            {
                std::cout << x << " ";
            }
            std::cout << "\n"
                      << sz(p2) << "\n";
            for (int x : p2)
            {
                std::cout << x << " ";
            }

            exit(0);
        }
    }
}

