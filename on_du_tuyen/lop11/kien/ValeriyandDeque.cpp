    {
        int a = dq.front();
        dq.pop_front();
        int b = dq.front();
        dq.pop_front();
        ans.push_back({a, b});
        if (b > a)
        {
            std::swap(a, b);
        }
        dq.push_front(a);
        dq.push_back(b);
    }

