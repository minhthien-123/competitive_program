ll add(ll x, ll y)
{
    return (x + y + 2 * mod) % mod;
}
ll sub(ll x, ll y)
{
    return (x - y + 2 * mod) % mod;
}
ll mul(ll x, ll y)
{
    return (x * y) % mod;
}
ll power(ll x, ll y)
{
    if (y == 0)
    {
        return 1;
    }
    ll tmp = power(x, y / 2);
    if (y % 2 == 0)
    {
        return (tmp * tmp) % mod;
    }
    else
    {
        return tmp * tmp % mod * x % mod;
    }
}

std::vector<int> adj[maxn + 7];
int n, m, k;
int par[maxn + 7], sz[maxn + 7];
ii query[maxn + 7];
bool flag[maxn + 7];

struct DSU
{
    int par[maxn + 7], sz[maxn + 7];

    DSU(int n)
    {
        for (int i = 0; i <= n; i++)
        {
            par[i] = i;
            sz[i] = 1;
        }
    }

    int acs(int v)
    {
        return v == par[v] ? v : par[v] = acs(par[v]);
    }

    void join(int a, int b)
    {
        a = acs(a);
        b = acs(b);
        if (a != b)
        {
            if (sz[a] < sz[b])
            {
                std::swap(a, b);
            }
            par[b] = a;
            sz[a] += sz[b];
        }
    }
};

