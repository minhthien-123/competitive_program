ll add(ll x, ll y)
{
    return (x + y + 2 * mod) % mod;
}
ll sub(ll x, ll y)
{
    return (x - y + 2 * mod) % mod;
}
ll mul(ll x, ll y)
{
    return (x * y) % mod;
}
ll power(ll x, ll y)
{
    if (y == 0)
    {
        return 1;
    }
    ll tmp = power(x, y / 2);
    if (y % 2 == 0)
    {
        return (tmp * tmp) % mod;
    }
    else
    {
        return tmp * tmp % mod * x % mod;
    }
}

std::vector<int> adj[maxn + 7];
int n, q;
int lazy[maxn + 8];

struct node
{
    int c0, c1, c2;
} st[4 * maxn + 7];

node merge(node a, node b)
{
    node res;
    res.c0 = a.c0 + b.c0;
    res.c1 = a.c1 + b.c1;
    res.c2 = a.c2 + b.c2;
    return res;
}

void build(int id, int l, int r)
{
    if (l == r)
    {
        st[id] = {1, 0, 0};
        return;
    }

    int mid = (l + r) / 2;
    build(id * 2, l, mid);
    build(id * 2 + 1, mid + 1, r);
    st[id] = merge(st[id * 2], st[id * 2 + 1]);
}

void shift(int id, int k)
{
    k %= 3;
    for (int i = 0; i < k; i++)
    {
        int tmp = st[id].c2;
        st[id].c2 = st[id].c1;
        st[id].c1 = st[id].c0;
        st[id].c0 = tmp;
    }
}

void fixed(int id)
{
    if (lazy[id] != 0)
    {
        lazy[2 * id] += lazy[id];
        lazy[2 * id + 1] += lazy[id];

        shift(2 * id, lazy[id]);
        shift(2 * id + 1, lazy[id]);

        lazy[id] = 0;
    }
}

void update(int id, int l, int r, int u, int v)
{
    if (u > r || v < l)
    {
        return;
    }
    if (u <= l && r <= v)
    {
        shift(id, 1);
        lazy[id] += 1;
        return;
    }

    fixed(id);
    int mid = (l + r) / 2;
    update(2 * id, l, mid, u, v);
    update(2 * id + 1, mid + 1, r, u, v);
    st[id] = merge(st[id * 2], st[id * 2 + 1]);
}

int get(int id, int l, int r, int u, int v)
{
    if (v < l || r < u)
    {
        return 0;
    }
    if (u <= l && r <= v)
    {
        return st[id].c0;
    }

    fixed(id);
    int mid = (l + r) / 2;
    int get1 = get(2 * id, l, mid, u, v);
    int get2 = get(2 * id + 1, mid + 1, r, u, v);
    return get1 + get2;
}

