const int maxn = 1e3;
const int inf = 1e9;
const int mod = 1e9 + 7;
const int inv = (mod + 1) / 2;
const int lg = 20;
const int dx[] = {0, 1, 0, -1, -1, -1, 1, 1};
const int dy[] = {1, 0, -1, 0, -1, 1, -1, 1};

ll add(ll x, ll y)
{
    return (x + y + 2 * mod) % mod;
}
ll sub(ll x, ll y)
{
    return (x - y + 2 * mod) % mod;
}
ll mul(ll x, ll y)
{
    return (x * y) % mod;
}
ll power(ll x, ll y)
{
    if (y == 0)
    {
        return 1;
    }
    ll tmp = power(x, y / 2);
    if (y % 2 == 0)
    {
        return (tmp * tmp) % mod;
    }
    else
    {
        return tmp * tmp % mod * x % mod;
    }
}

std::vector<int> adj[maxn + 7];
int n, p;
int a[maxn + 7];
int dp[maxn + 7][maxn + 7];
int ans = -inf;

void dfs(int u, int par)
{
    dp[u][1] = a[u];
    for (int v : adj[u])
    {
        if (v == par)
        {
            continue;
        }
        dfs(v, u);

        for (int d = 1; d <= p; d++)
        {
            ans = std::max(ans, dp[u][p - d] + dp[v][d - 1]);
        }

        for (int d = 1; d <= p; d++)
        {
            dp[u][d] = std::max(dp[u][d], dp[v][d - 1] + a[u]);
        }
    }
}

