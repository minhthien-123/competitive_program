const int maxn = 1e5;
const ll inf = 1e18;
const int mod = 1e9 + 7;
const int inv = (mod + 1) / 2;
const int lg = 20;

ll add(ll x, ll y)
{
    return (x + y + 2 * mod) % mod;
}
ll sub(ll x, ll y)
{
    return (x - y + 2 * mod) % mod;
}
ll mul(ll x, ll y)
{
    return (x * y) % mod;
}
ll power(ll x, ll y)
{
    if (y == 0)
    {
        return 1;
    }
    ll tmp = power(x, y / 2);
    if (y % 2 == 0)
    {
        return (tmp * tmp) % mod;
    }
    else
    {
        return tmp * tmp % mod * x % mod;
    }
}

std::vector<pll> adj[maxn + 7];
int n, m, k, A, B;
ll cost[maxn + 7];
ll da[maxn + 7], db[maxn + 7];

void dijkstra(int s, ll d[])
{
    for (int i = 1; i <= n; i++)
    {
        d[i] = inf;
    }
    d[s] = 0;
    std::priority_queue<pll, std::vector<pll>, std::greater<pll>> pq;
    pq.push({0, s});

    while (sz(pq))
    {
        auto [du, u] = pq.top();
        pq.pop();

        if (du > d[u])
        {
            continue;
        }

        for (auto e : adj[u])
        {
            auto [v, w] = e;
            if (d[u] + w < d[v])
            {
                d[v] = d[u] + w;
                pq.push({d[v], v});
            }
        }
    }
}

