ll add(ll x, ll y)
{
    return (x + y + 2 * mod) % mod;
}
ll sub(ll x, ll y)
{
    return (x - y + 2 * mod) % mod;
}
ll mul(ll x, ll y)
{
    return (x * y) % mod;
}
ll power(ll x, ll y)
{
    if (y == 0)
    {
        return 1;
    }
    ll tmp = power(x, y / 2);
    if (y % 2 == 0)
    {
        return (tmp * tmp) % mod;
    }
    else
    {
        return tmp * tmp % mod * x % mod;
    }
}

int n, m;
int a[maxn + 7];

struct DSU
{
    std::vector<int> par, sz;
    std::vector<std::map<int, int>> mp;
    ll ans;

    DSU(int n)
    {
        par.resize(n + 7);
        sz.resize(n + 7);
        mp.resize(n + 7);
        ans = 0;

        for (int i = 1; i <= n; i++)
        {
            par[i] = i;
            sz[i] = 1;
            mp[i][a[i]] = 1;
        }
    }

    int acs(int v)
    {
        return v == par[v] ? v : par[v] = acs(par[v]);
    }

    void join(int u, int v)
    {
        u = acs(u);
        v = acs(v);
        if (u != v)
        {
            if (sz[u] < sz[v])
            {
                std::swap(u, v);
            }
            par[v] = u;
            sz[u] += sz[v];

            if (sz(mp[u]) < sz(mp[v]))
            {
                std::swap(mp[u], mp[v]);
            }

            for (auto [color, cnt] : mp[v])
            {
                ans += 1LL * mp[u][color] * cnt;
                mp[u][color] += cnt;
            }
            mp[v].clear();
        }
    }
};

