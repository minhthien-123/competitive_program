ll add(ll x, ll y)
{
    return (x + y + 2 * mod) % mod;
}
ll sub(ll x, ll y)
{
    return (x - y + 2 * mod) % mod;
}
ll mul(ll x, ll y)
{
    return (x * y) % mod;
}
ll power(ll x, ll y)
{
    if (y == 0)
    {
        return 1;
    }
    ll tmp = power(x, y / 2);
    if (y % 2 == 0)
    {
        return (tmp * tmp) % mod;
    }
    else
    {
        return tmp * tmp % mod * x % mod;
    }
}

std::vector<int> adj[maxn + 7];
int n, m, k;
int par[maxn + 7];
int cnt_y[maxn + 7];
int cnt_g[maxn + 7];

int ansy = 0, ansg = 0;

void init(int v)
{
    par[v] = v;
    if (v <= k)
    {
        cnt_y[v] = 1;
        cnt_g[v] = 0;
    }
    else
    {
        cnt_y[v] = 0;
        cnt_g[v] = 1;
    }
}

int acs(int v)
{
    if (v == par[v])
    {
        return v;
    }
    return par[v] = acs(par[v]);
}

void join(int a, int b, int id)
{
    a = acs(a);
    b = acs(b);
    if (a != b)
    {
        par[b] = a;

        cnt_y[a] += cnt_y[b];
        cnt_g[a] += cnt_g[b];

        if (cnt_y[a] == k && ansy == 0)
        {
            ansy = id;
        }

        if (cnt_g[a] == n - k && ansg == 0)
        {
            ansg = id;
        }
    }
}

