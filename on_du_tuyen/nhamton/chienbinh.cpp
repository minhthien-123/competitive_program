ll add(ll x, ll y)
{
    return (x + y + 2 * mod) % mod;
}
ll sub(ll x, ll y)
{
    return (x - y + 2 * mod) % mod;
}
ll mul(ll x, ll y)
{
    return (x * y) % mod;
}
ll power(ll x, ll y)
{
    if (y == 0)
    {
        return 1;
    }
    ll tmp = power(x, y / 2);
    if (y % 2 == 0)
    {
        return (tmp * tmp) % mod;
    }
    else
    {
        return tmp * tmp % mod * x % mod;
    }
}

struct chienbinh
{
    int l, r, x;
} query[maxn + 7];

std::vector<int> adj[maxn + 7];
int n, m;
int par[maxn + 7], ans[maxn + 7];

struct DSU
{
    std::vector<int> par, sz;

    DSU(int n)
    {
        par.resize(n + 7);
        sz.resize(n + 7);

        for (int i = 0; i <= n + 1; i++)
        {
            par[i] = i;
            sz[i] = 1;
        }
    }

    int acs(int v)
    {
        return v == par[v] ? v : par[v] = acs(par[v]);
    }

    void join(int u, int v)
    {
        u = acs(u);
        v = acs(v);
        if (u != v)
        {
            if (sz[u] < sz[v])
            {
                std::swap(u, v);
            }
            par[v] = u;
            sz[u] += sz[v];
        }
    }
};

