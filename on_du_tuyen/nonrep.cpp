int n;
bool vis[10];
int res[20];
std::string s;

bool check(int x)
{
    memset(vis, false, sizeof vis);
    while (x > 0)
    {
        int tmp = x % 10;
        if (vis[tmp])
        {
            return false;
        }
        if (tmp == 0)
        {
            return false;
        }
        vis[tmp] = true;
        x /= 10;
    }
    return true;
}

void sub1()
{
    while (!check(n))
    {
        n++;
    }
    std::cout << n << "\n";
}

bool solve(int pos)
{
    int cur = s[pos] - '0';
    for (int i = std::max(1, cur); i <= 9; ++i)
    {
        if (vis[i])
        {
            continue;
        }
        res[pos] = i;
        vis[i] = true;

        if (i > cur)
        {
            int idx = pos + 1;
            for (int j = 1; j <= 9 && idx < sz(s); j++)
            {
                if (!vis[j])
                {
                    res[idx++] = j;
                }
            }
            if (idx == sz(s))
            {
                return true;
            }
        }
        else
        {
            if (solve(pos + 1))
            {
                return true;
            }
        }

        vis[i] = false;
    }

    return false;
}

void sub2()
{
    int m = n;
    s.clear();
    while (m)
    {
        int tmp = m % 10;
        s.push_back(char(tmp + '0'));
        m /= 10;
    }
    std::reverse(s.begin(), s.end());

    memset(res, -1, sizeof res);
    memset(vis, false, sizeof vis);

    if (sz(s) > 9)
    {
        std::cout << "0\n";
    }
    else if (solve(0))
    {
        for (int i = 0; i < sz(s); i++)
        {
            std::cout << res[i];
        }
    }
    else
    {
        for (int i = 1; i <= sz(s) + 1; ++i)
        {
            std::cout << i;
        }
    }
    std::cout << "\n";
}

