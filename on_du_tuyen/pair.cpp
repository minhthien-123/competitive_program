ll add(ll x, ll y)
{
    return (x + y + 2 * mod) % mod;
}
ll sub(ll x, ll y)
{
    return (x - y + 2 * mod) % mod;
}
ll mul(ll x, ll y)
{
    return (x * y) % mod;
}
ll power(ll x, ll y)
{
    if (y == 0)
    {
        return 1;
    }
    ll tmp = power(x, y / 2);
    if (y % 2 == 0)
    {
        return (tmp * tmp) % mod;
    }
    else
    {
        return tmp * tmp % mod * x % mod;
    }
}

std::vector<int> adj[maxn + 7];
int n, m, k;
ll a[maxn + 7], b[maxn + 7];
ll dp[1007][1007];

bool check_sub2(ll mid)
{
    dp[0][0] = 0;
    for (int i = 1; i <= n; i++)
    {
        dp[i][0] = 0;
    }
    for (int j = 1; j <= m; j++)
    {
        dp[0][j] = 0;
    }
    for (int i = 1; i <= n; i++)
    {
        for (int j = 1; j <= m; j++)
        {
            dp[i][j] = std::max(dp[i - 1][j], dp[i][j - 1]);
            if (llabs(a[i] - b[j]) <= mid)
            {
                dp[i][j] = std::max(dp[i][j], dp[i - 1][j - 1] + 1);
            }
        }
    }
    return dp[n][m] >= k;
}

void sub2()
{
    ll l = 0, r = 1e18, ans = -1;
    while (l <= r)
    {
        ll mid = (l + r) / 2;
        if (check_sub2(mid))
        {
            ans = mid;
            r = mid - 1;
        }
        else
        {
            l = mid + 1;
        }
    }
    std::cout << ans;
}

bool check_sub3(ll mid)
{
    int i = 1, j = 1;
    int cnt = 0;
    while (i <= n && j <= m)
    {
        if (llabs(a[i] - b[j]) <= mid)
        {
            cnt++;
            i++;
            j++;
        }
        else
        {
            if (a[i] < b[j])
            {
                i++;
            }
            else
            {
                j++;
            }
        }
    }
    return cnt >= k;
}

void sub3()
{
    ll l = 0, r = 1e18, ans = -1;
    while (l <= r)
    {
        ll mid = (l + r) / 2;
        if (check_sub3(mid))
        {
            ans = mid;
            r = mid - 1;
        }
        else
        {
            l = mid + 1;
        }
    }
    std::cout << ans;
}

