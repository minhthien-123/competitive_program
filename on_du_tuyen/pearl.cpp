const int maxn = 1e6;
const int mx   = 19 * 81;
const ll inf = 1e18;
const int mod = 1e9 + 7;
const int inv = (mod + 1) / 2;
const int lg = 20;

ll add(ll x, ll y)
{
    return (x + y + 2 * mod) % mod;
}
ll sub(ll x, ll y)
{
    return (x - y + 2 * mod) % mod;
}
ll mul(ll x, ll y)
{
    return (x * y) % mod;
}
ll power(ll x, ll y)
{
    if (y == 0)
    {
        return 1;
    }
    ll tmp = power(x, y / 2);
    if (y % 2 == 0)
    {
        return (tmp * tmp) % mod;
    }
    else
    {
        return tmp * tmp % mod * x % mod;
    }
}

std::vector<int> adj[maxn + 7];
int n, t;

int f[maxn + 7];

int sum(int x)
{
    int s = 0;
    while (x > 0)
    {
        int d = x % 10;
        s += d * d;
        x /= 10;
    }
    return s;
}

void init()
{
    for (int i = 0; i <= mx; i++)
    {
        f[i] = -1;
    }
    f[1] = 1;
    for (int s = 0; s <= mx; s++)
    {
        if (f[s] != -1)
        {
            continue;
        }
        std::vector<int> path;
        int cur = s;
        std::unordered_map<int, int> mp;

        while (true)
        {
            if (cur <= mx && f[cur] != -1)
            {
                int val = f[cur];
                for (int v : path)
                {
                    if (v <= mx)
                    {
                        f[v] = val;
                    }
                }
                break;
            }

            if (mp.find(cur) != mp.end())
            {
                for (int v : path)
                {
                    if (v <= mx)
                    {
                        f[v] = 0;
                    }
                }
                break;
            }

            mp[cur] = path.size();
            path.pb(cur);
            cur = sum(cur);
        }
    }

    for (int i = 0; i <= mx; i++)
    {
        if (f[i] == -1)
        {
            f[i] = 0;
        }
    }
}

std::string digit;
int dp[20][mx + 1];
bool vis[20][mx + 1];

int solve(int pos, int sum, bool tight)
{
    if (pos == digit.size())
    {
        return f[sum] == 0;
    }

    if (!tight && vis[pos][sum])
    {
        return dp[pos][sum];
    }
    int lim = tight ? (digit[pos] - '0') : 9;
    int res = 0;
    for (int d = 0; d <= lim; d++)
    {
        bool nt = tight && (d == lim);
        res += solve(pos + 1, sum + d * d, nt);
    }

    if (!tight)
    {
        vis[pos][sum] = true;
        dp[pos][sum] = res;
    }

    return res;
}

int get(int x)
{
    if (x < 0)
    {
        return 0;
    }
    digit = std::to_string(x);
    memset(vis, 0, sizeof vis);
    memset(dp, 0, sizeof dp);
    return solve(0, 0, 1);
}

