ll power(ll x, ll y)
{
    if (y == 0)
    {
        return 1;
    }
    ll tmp = power(x, y / 2);
    if (y % 2 == 0)
    {
        return (tmp * tmp) % mod;
    }
    else
    {
        return tmp * tmp % mod * x % mod;
    }
}

std::vector<int> adj[maxn + 7];
int n, k;
ll a[maxn + 7], b[maxn + 7];
ll f[maxn + 7];
int dp[maxn + 7];

int lis(ll a[])
{
    memset(dp, 0, sizeof dp);
    memset(f, 0, sizeof f);
    int ans = 0;
    for (int i = 1; i <= n; i++)
    {
        dp[i] = std::lower_bound(f + 1, f + ans + 1, a[i]) - f;
        if (dp[i] > ans)
        {
            ans++;
        }
        f[dp[i]] = a[i];
    }

    return ans;
}

void sub1()
{
    int ans = lis(a);
    for (int l = 1; l <= n; l++)
    {
        for (int r = l + 1; r <= n; r++)
        {
            for (ll x = -k; x <= k; x++)
            {
                for (int i = 1; i <= n; i++)
                {
                    if (l <= i && r <= i)
                    {
                        b[i] = a[i] + x;
                    }
                    else
                    {
                        b[i] = a[i];
                    }
                }
                ans = std::max(ans, lis(b));
            }
        }
    }
    std::cout << ans;
}

