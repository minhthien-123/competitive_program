const int mod = 1e9 + 7;
const int inv = (mod + 1) / 2;
const int lg = 20;

ll add(ll x, ll y)
{
    return (x + y + 2 * mod) % mod;
}
ll sub(ll x, ll y)
{
    return (x - y + 2 * mod) % mod;
}
ll mul(ll x, ll y)
{
    return (x * y) % mod;
}
ll power(ll x, ll y)
{
    if (y == 0)
    {
        return 1;
    }
    ll tmp = power(x, y / 2);
    if (y % 2 == 0)
    {
        return (tmp * tmp) % mod;
    }
    else
    {
        return tmp * tmp % mod * x % mod;
    }
}

std::vector<int> adj[maxn + 7];
int n, k;
int par[maxn + 7];
int flag[maxn + 7];
int bit[maxn + 7];
int ans = 0;

void update(int id, int val)
{
    for (; id <= n; id += id & -id)
    {
        bit[id] += val;
    }
}

int get(int id)
{
    int res = 0;
    for (; id > 0; id -= id & -id)
    {
        res += bit[id];
    }
    return res;
}

void dfs(int u)
{
    int l = std::max(1LL, u - k);
    int r = std::min(n, u + k);
    ans += get(r) - get(l - 1);
    update(u, 1);
    for (int v : adj[u])
    {
        dfs(v);
    }
    update(u, -1);
}

