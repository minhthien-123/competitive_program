const int mod = 1e9 + 7;
const int inv = (mod + 1) / 2;
const int lg = 20;

ll add(ll x, ll y)
{
    return (x + y + 2 * mod) % mod;
}
ll sub(ll x, ll y)
{
    return (x - y + 2 * mod) % mod;
}
ll mul(ll x, ll y)
{
    return (x * y) % mod;
}
ll power(ll x, ll y)
{
    if (y == 0)
    {
        return 1;
    }
    ll tmp = power(x, y / 2);
    if (y % 2 == 0)
    {
        return (tmp * tmp) % mod;
    }
    else
    {
        return tmp * tmp % mod * x % mod;
    }
}

std::vector<int> adj[maxn + 7];
int n;
ll a[maxn + 7], f[maxn + 7], invf[maxn + 7];

ll C(int n, int k)
{
    if (k > n || k < 0)
    {
        return 0;
    }
    return mul(a[n], mul(invf[k], invf[n - k]));
}

void init()
{
    a[0] = 1;
    for (int i = 1; i <= 2 * n; i++)
    {
        a[i] = mul(a[i - 1], i);
    }
    invf[2 * n] = power(a[2 * n], mod - 2);
    for (int i = 2 * n - 1; i >= 0; i--)
    {
        invf[i] = mul(invf[i + 1], i + 1);
    }
}

