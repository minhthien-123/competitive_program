ll add(ll x, ll y)
{
    return (x + y + 2 * mod) % mod;
}
ll sub(ll x, ll y)
{
    return (x - y + 2 * mod) % mod;
}
ll mul(ll x, ll y)
{
    return (x * y) % mod;
}
ll power(ll x, ll y)
{
    if (y == 0)
    {
        return 1;
    }
    ll tmp = power(x, y / 2);
    if (y % 2 == 0)
    {
        return (tmp * tmp) % mod;
    }
    else
    {
        return tmp * tmp % mod * x % mod;
    }
}

std::vector<int> adj[maxn + 7];
int t, n;
ll cnt1 = 1, cnt2 = 1, a[maxn + 7][maxn + 7];
ll mp[maxn * maxn + 7];
ll dp[maxn + 7][maxn + 7];

void init()
{
    for (int i = 1; i <= 2023; i++)
    {
        for (int j = 1; j <= cnt1; j++)
        {
            a[i][j] = cnt2 * cnt2;
            cnt2++;
        }
        cnt1++;
    }
    cnt1 = 1;
    for (int i = 1; i <= 2023; i++)
    {
        for (int j = 1; j <= cnt1; j++)
        {
            dp[i][j] = dp[i - 1][j] + dp[i - 1][j - 1] + a[i][j];
            if (i >= 2)
            {
                dp[i][j] = dp[i][j] - dp[i - 2][j - 1];
            }
            mp[(int)sqrt(a[i][j])] = dp[i][j];
        }
        cnt1++;
    }
}

