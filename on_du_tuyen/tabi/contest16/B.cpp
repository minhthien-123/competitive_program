{
    std::ios_base::sync_with_stdio(0);
    std::cin.tie(0);
    std::cout.tie(0);

    if (std::fopen(task ".inp", "r"))
    {
        std::freopen(task ".inp", "r", stdin);
        std::freopen(task ".out", "w", stdout);
    }

    std::cin >> n >> x;
    for (int i = 1; i < n; i++)
    {
        int u, v;
        std::cin >> u >> v;
        adj[u].push_back(v);
        adj[v].push_back(u);
    }

    dfs(1, 0, 0, d1);
    dfs(x, 0, 0, d2);

    int ans = 0;
    for (int i = 1; i <= n; i++)
    {
        if (d2[i] < d1[i])
        {
            ans = std::max(ans, d1[i]);
        }
    }

    std::cout << ans * 2 << "\n";

    std::cerr << "\nTime elapsed: " << TIME << " s.\n";

    return 0;
}
