ll add(ll x, ll y)
{
    return (x + y + 2 * mod) % mod;
}
ll sub(ll x, ll y)
{
    return (x - y + 2 * mod) % mod;
}
ll mul(ll x, ll y)
{
    return (x * y) % mod;
}
ll power(ll x, ll y)
{
    if (y == 0)
    {
        return 1;
    }
    ll tmp = power(x, y / 2);
    if (y % 2 == 0)
    {
        return (tmp * tmp) % mod;
    }
    else
    {
        return tmp * tmp % mod * x % mod;
    }
}

int n;
int ind[maxn + 7], outd[maxn + 7];
bool used[maxn + 7];
std::multiset<int> adj[maxn + 7];
std::vector<int> ans;

int get_id(char c)
{
    if (c >= 'a' && c <= 'z')
    {
        return c - 'a';
    }
    if (c >= 'A' && c <= 'Z')
    {
        return c - 'A' + 26;
    }
    return c - '0' + 52;
}

char get_char(int id)
{
    if (id < 26)
    {
        return id + 'a';
    }
    if (id < 52)
    {
        return id - 26 + 'A';
    }
    return id - 52 + '0';
}

int get(char c1, char c2)
{
    return get_id(c1) * 62 + get_id(c2);
}

void dfs(int u)
{
    while (!adj[u].empty())
    {
        int v = *adj[u].begin();
        adj[u].erase(adj[u].begin());
        dfs(v);
    }
    ans.pb(u);
}

