{
    std::ios_base::sync_with_stdio(0);
    std::cin.tie(0);
    std::cout.tie(0);

    if (std::fopen(task ".inp", "r"))
    {
        std::freopen(task ".inp", "r", stdin);
        std::freopen(task ".out", "w", stdout);
    }

    std::cin >> n;

    for (int i = 1; i <= n; ++i)
    {
        std::string s;
        std::cin >> s;
        int u = get(s[0], s[1]);
        int v = get(s[1], s[2]);
        adj[u].ins(v);
        outd[u]++;
        ind[v]++;
    }

    int s = -1;
    int t = -1;
    bool check = false;
    int node = -1;

    for (int i = 0; i < 3844; i++)
    {
        if (outd[i] > 0)
        {
            node = i;
        }
        if (outd[i] - ind[i] == 1)
        {
            if (s != -1)
            {
                check = true;
            }
            s = i;
        }
        else if (ind[i] - outd[i] == 1)
        {
            if (t != -1)
            {
                check = true;
            }
            t = i;
        }
        else if (ind[i] != outd[i])
        {
            check = true;
        }
    }

    if (check)
    {
        std::cout << "NO\n";
        return 0;
    }

    if (s == -1)
    {
        s = node;
    }

    dfs(s);

    if (sz(ans) != n + 1)
    {
        std::cout << "NO\n";
        return 0;
    }

    std::reverse(ans.begin(), ans.end());
    std::cout << "YES\n";

    std::string res = "";
    res += get_char(ans[0] / 62);
    res += get_char(ans[0] % 62);
    for (int i = 1; i < sz(ans); i++)
    {
        res += get_char(ans[i] % 62);
    }
    std::cout << res << "\n";

    std::cerr << "\nTime elapsed: " << TIME << " s.\n";

    return 0;
}
