ll add(ll x, ll y)
{
    return (x + y + 2 * mod) % mod;
}
ll sub(ll x, ll y)
{
    return (x - y + 2 * mod) % mod;
}
ll mul(ll x, ll y)
{
    return (x * y) % mod;
}

std::vector<int> adj[maxn + 7];
ll n, F[2][2], M[2][2];

void fibonacci(ll f1[2][2], ll f2[2][2], ll mod)
{
    ll a = (f1[0][0] * f2[0][0] % mod + f1[0][1] * f2[1][0] % mod) % mod;
    ll b = (f1[0][0] * f2[0][1] % mod + f1[0][1] * f2[1][1] % mod) % mod;
    ll c = (f1[1][0] * f2[0][0] % mod + f1[1][1] * f2[1][0] % mod) % mod;
    ll d = (f1[1][0] * f2[0][1] % mod + f1[1][1] * f2[1][1] % mod) % mod;
    F[0][0] = a;
    F[0][1] = b;
    F[1][0] = c;
    F[1][1] = d;
}

void power(ll f[2][2], ll n)
{
    if (n <= 1)
    {
        return;
    }
    power(f, n / 2);
    fibonacci(f, f, mod);
    if (n % 2 != 0)
    {
        fibonacci(f, M, mod);
    }
}

