const int maxn = 200005;
const ll inf = 1e18;
const int mod = 1e9 + 7;
const int inv = (mod + 1) / 2;
const int lg = 20;

ll add(ll x, ll y)
{
    return (x + y + 2 * mod) % mod;
}
ll sub(ll x, ll y)
{
    return (x - y + 2 * mod) % mod;
}
ll mul(ll x, ll y)
{
    return (x * y) % mod;
}
ll power(ll x, ll y)
{
    if (y == 0)
    {
        return 1;
    }
    ll tmp = power(x, y / 2);
    if (y % 2 == 0)
    {
        return (tmp * tmp) % mod;
    }
    else
    {
        return tmp * tmp % mod * x % mod;
    }
}

int n;
std::vector<int> adj[maxn];
std::string str;

int subtree_sz[maxn];
bool deleted[maxn];
int freq[1 << 20];
int P[maxn];
ll cnt[maxn];
ll ans[maxn];

// Tinh kich thuoc cay con
void get_sz(int u, int p)
{
    subtree_sz[u] = 1;
    for (int v : adj[u])
    {
        if (v != p && !deleted[v])
        {
            get_sz(v, u);
            subtree_sz[u] += subtree_sz[v];
        }
    }
}

// Tim trong tam (Centroid)
int get_centroid(int u, int p, int total)
{
    for (int v : adj[u])
    {
        if (v != p && !deleted[v] && subtree_sz[v] > total / 2)
        {
            return get_centroid(v, u, total);
        }
    }
    return u;
}

// Tinh XOR sum tu u den centroid (khong bao gom centroid)
void get_paths(int u, int p, int mask, std::vector<int> &nodes)
{
    P[u] = mask;
    nodes.pb(u);
    for (int v : adj[u])
    {
        if (v != p && !deleted[v])
        {
            get_paths(v, u, mask ^ (1 << (str[v - 1] - 'a')), nodes);
        }
    }
}

// Day dp len tren theo nhanh cay con (tinh so lan duong di di qua tung nut)
ll dfs_sum(int u, int p)
{
    ll sum = cnt[u];
    for (int v : adj[u])
    {
        if (v != p && !deleted[v])
        {
            sum += dfs_sum(v, u);
        }
    }
    ans[u] += sum;
    return sum;
}

// Xy ly Centroid Decomposition
void solve(int u)
{
    get_sz(u, 0);
    int centroid = get_centroid(u, 0, subtree_sz[u]);

    std::vector<int> comp_nodes;
    P[centroid] = 0;
    comp_nodes.pb(centroid);
    freq[0]++;

    std::vector<std::vector<int>> subtrees;
    for (int v : adj[centroid])
    {
        if (!deleted[v])
        {
            std::vector<int> sub_nodes;
            get_paths(v, centroid, 1 << (str[v - 1] - 'a'), sub_nodes);
            for (int x : sub_nodes)
                freq[P[x]]++;
            for (int x : sub_nodes)
                comp_nodes.pb(x);
            subtrees.pb(sub_nodes);
        }
    }

    // Tap hop cac mask hop le can tim (co toi da 1 bit 1) de ghep thanh xau doi xung
    int c_char_mask = 1 << (str[centroid - 1] - 'a');
    std::vector<int> req_masks;
    req_masks.pb(c_char_mask);
    for (int i = 0; i < 20; ++i)
    {
        req_masks.pb((1 << i) ^ c_char_mask);
    }

    ll total_C = 0;

    for (const auto &sub_nodes : subtrees)
    {
        // Loai bo tan so cua cay con hien tai de tranh ghep 2 nut cung 1 nhanh
        for (int x : sub_nodes)
            freq[P[x]]--;

        for (int x : sub_nodes)
        {
            cnt[x] = 0;
            for (int req : req_masks)
            {
                cnt[x] += freq[P[x] ^ req];
            }
            total_C += cnt[x];
        }

        // Them lai vao mang tan so
        for (int x : sub_nodes)
            freq[P[x]]++;
    }

    // Xet rieng cac duong di bat dau tu chinh Centroid
    cnt[centroid] = 0;
    for (int req : req_masks)
    {
        cnt[centroid] += freq[req];
    }
    total_C += cnt[centroid];

    // Cap nhat so luong duong di di qua centroid
    // Cong 1 de tinh truong hop duong di gom 1 nut (chinh no)
    ans[centroid] += (total_C + 1) / 2;

    // Phan phoi duong di cho cac nut trong nhanh
    for (int v : adj[centroid])
    {
        if (!deleted[v])
        {
            dfs_sum(v, centroid);
        }
    }

    // Don dep lai mang tan so cho lan sau (Dam bao O(N))
    for (int x : comp_nodes)
        freq[P[x]] = 0;

    // Tiep tuc de quy chia de tri cac cay con
    deleted[centroid] = true;
    for (int v : adj[centroid])
    {
        if (!deleted[v])
            solve(v);
    }
}

