const int inv = (mod + 1) / 2;
const int lg = 20;

int n, m;
int r[maxn];
std::vector<int> a[maxn];
int par[maxn];

int acs(int u)
{
    if (u == par[u])
    {
        return u;
    }
    return par[u] = acs(par[u]);
}

void join(int u, int v)
{
    u = acs(u);
    v = acs(v);
    if (u != v)
    {
        par[u] = v;
    }
}

