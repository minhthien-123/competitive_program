#include <bits/stdc++.h>
#define task ""
#define int long long
#define ii std::pair<int, int>
#define iii std::pair<ii, int>
#define fi first
#define se second
#define pb push_back
#define ins insert

const int maxn = 1e5;
const int inf  = 1e18;
const int mod  = 1e9 + 7;
const int inv  = (mod + 1) / 2;
const int bl   = 320;

int n, q;
std::vector<int> adj[maxn + 7];
int up[maxn + 7][20];
int h[maxn + 7];
int d[maxn + 7];
bool flag[maxn + 7];
std::vector<int> vec;

void dfs(int u)
{
    for (int v : adj[u])
    {
        if (up[u][0] == v)
        {
            continue;
        }

        h[v] = h[u] + 1;
        up[v][0] = u;

        for (int j = 1; j <= 19; j++)
        {
            up[v][j] = up[up[v][j - 1]][j - 1];
        }

        dfs(v);
    }
}

int lca(int u, int v)
{
    if (h[u] != h[v])
    {
        if (h[u] < h[v])
        {
            std::swap(u, v);
        }

        int k = h[u] - h[v];
        for (int j = 0; (1 << j) <= k; j++)
        {
            if (k >> j & 1)
            {
                u = up[u][j];
            }
        }
    }

    if (u == v)
    {
        return u;
    }

    for (int j = 19; j >= 0; j--)
    {
        if (up[u][j] != up[v][j])
        {
            u = up[u][j];
            v = up[v][j];
        }
    }

    return up[u][0];
}

int dist(int u, int v)
{
    int p = lca(u, v);
    return h[u] + h[v] - 2 * h[p];
}

void bfs()
{
    std::queue<int> qu;
    for (int i = 1; i <= n; i++)
    {
        if (flag[i])
        {
            d[i] = 0;
            qu.push(i);
        }
    }

    while (qu.size())
    {
        int u = qu.front();
        qu.pop();
        for (int v : adj[u])
        {
            if (d[v] > d[u] + 1)
            {
                d[v] = d[u] + 1;
                qu.push(v);
            }
        }
    }

    vec.clear();
}

signed main()
{
    std::ios_base::sync_with_stdio(0);
    std::cin.tie(0); std::cout.tie(0);

    if (std::fopen(task".inp", "r"))
    {
        std::freopen(task".inp", "r", stdin);
        std::freopen(task".out", "w", stdout);
    }

    std::cin >> n >> q;

    for (int i = 1; i < n; i++)
    {
        int u, v;
        std::cin >> u >> v;
        adj[u].pb(v);
        adj[v].pb(u);
    }

    h[1] = 0;
    up[1][0] = 1;
    dfs(1);

    for (int i = 1; i <= n; i++)
    {
        d[i] = inf;
        flag[i] = false;
    }

    flag[1] = true;
    bfs();

    while (q--)
    {
        int type, v;
        std::cin >> type >> v;

        if (type == 1)
        {
            flag[v] = true;
            vec.pb(v);
            if (vec.size() >= bl)
            {
                bfs();
            }
        }
        else
        {
            int ans = d[v];
            for (int u : vec)
            {
                ans = std::min(ans, dist(u, v));
            }
            std::cout << ans << "\n";
        }
    }

    return 0;
}
