void solve()
{
    int n;
    char c;
    std::cin >> n >> c;
    std::string s;
    std::cin >> s;
    s = " " + s;

    bool ok = true;
    for (int i = 1; i <= n; i++)
    {
        if (s[i] != c)
        {
            ok = false;
            break;
        }
    }

    if (ok)
    {
        std::cout << 0 << "\n";
        return;
    }

    for (int x = 1; x <= n; x++)
    {
        bool check = true;
        for (int i = x; i <= n; i += x)
        {
            if (s[i] != c)
            {
                check = false;
                break;
            }
        }
        if (check)
        {
            std::cout << 1 << "\n";
            std::cout << x << "\n";
            return;
        }   
    }

    std::cout << 2 << "\n";
    std::cout << n - 1 << " " << n << "\n";
}

signed main()
{
    std::ios_base::sync_with_stdio(0);
    std::cin.tie(0);
    std::cout.tie(0);

    if (std::fopen(task ".inp", "r"))
    {
        std::freopen(task ".inp", "r", stdin);
        std::freopen(task ".out", "w", stdout);
    }

    int t;
    std::cin >> t;
    while (t--)
    {
        solve();
    }

    return 0;
}
