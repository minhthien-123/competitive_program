{
    std::ios_base::sync_with_stdio(0);
    std::cin.tie(0);
    std::cout.tie(0);

    if (std::fopen(task ".inp", "r"))
    {
        std::freopen(task ".inp", "r", stdin);
        std::freopen(task ".out", "w", stdout);
    }

    int t;
    std::cin >> t;
    while (t--)
    {
        std::cin >> n >> m;
        edges.clear();

        for (int i = 0; i < m; i++)
        {
            int u, v, w;
            std::cin >> u >> v >> w;
            edges.pb({u, v, w});
        }

        int mask = 0;
        int ans = 0;

        for (int j = 29; j >= 0; j--)
        {
            int tmp = mask | (1LL << j);

            for (int i = 1; i <= n; i++)
            {
                par[i] = i;
            }

            int sz = n;
            for (int i = 0; i < m; i++)
            {
                if ((edges[i].w & tmp) == 0)
                {
                    if (join(edges[i].u, edges[i].v))
                    {
                        sz--;
                    }
                }
            }

            if (sz == 1)
            {
                mask = tmp;
            }
            else
            {
                ans |= (1LL << j);
            }
        }

        std::cout << ans << "\n";
    }

    std::cerr << "\nTime elapsed: " << TIME << " s.\n";

    return 0;
}
