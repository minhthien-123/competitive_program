int a[maxn + 7];
int ans[maxn + 7];
int f[maxn + 7];
int id = 0;

int calc(int k)
{
    int res = 1;
    int d = 0;
    id++;

    for (int i = 1; i <= n; i++)
    {
        if (f[a[i]] != id)
        {
            if (d == k)
            {
                res++;
                id++;
                d = 1;
                f[a[i]] = id;
            }
            else
            {
                d++;
                f[a[i]] = id;
            }
        }
    }
    return res;
}

