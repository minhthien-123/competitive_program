ll power(ll x, ll y)
{
    if (y == 0)
    {
        return 1;
    }
    ll tmp = power(x, y / 2);
    if (y % 2 == 0)
    {
        return (tmp * tmp) % mod;
    }
    else
    {
        return tmp * tmp % mod * x % mod;
    }
}

const int max_nodes = 2e6 + 5;
struct Trie
{
    int child[max_nodes][26];
    int is_end[max_nodes];
    int node_cnt;

    void init()
    {
        node_cnt = 0;
        memset(child[0], -1, sizeof(child[0]));
        is_end[0] = 0;
    }

    int new_node()
    {
        node_cnt++;
        memset(child[node_cnt], -1, sizeof(child[node_cnt]));
        is_end[node_cnt] = 0;
        return node_cnt;
    }

    void insert(std::string s)
    {
        int curr = 0;
        for (char c : s)
        {
            int id = c - 'a';
            if (child[curr][id] == -1)
            {
                child[curr][id] = new_node();
            }
            curr = child[curr][id];
        }
        is_end[curr]++;
    }

    bool search(std::string s)
    {
        int curr = 0;
        for (char c : s)
        {
            int id = c - 'a';
            if (child[curr][id] == -1)
            {
                return false;
            }
            curr = child[curr][id];
        }
        return is_end[curr] > 0;
    }
};

