const int max_nodes = 6000005;
struct Trie
{
    int child[max_nodes][2];
    int cnt[max_nodes];
    int node_cnt;

    void init()
    {
        node_cnt = 0;
        memset(child[0], -1, sizeof(child[0]));
        cnt[0] = 0;
    }

    int new_node()
    {
        node_cnt++;
        memset(child[node_cnt], -1, sizeof(child[node_cnt]));
        cnt[node_cnt] = 0;
        return node_cnt;
    }

    void insert(int x)
    {
        int curr = 0;
        for (int i = 29; i >= 0; i--)
        {
            int bit = (x >> i) & 1;
            if (child[curr][bit] == -1)
            {
                child[curr][bit] = new_node();
            }
            curr = child[curr][bit];
            cnt[curr]++;
        }
    }

    void remove(int x)
    {
        int curr = 0;
        for (int i = 29; i >= 0; i--)
        {
            int bit = (x >> i) & 1;
            curr = child[curr][bit];
            cnt[curr]--;
        }
    }

    int query(int x)
    {
        int curr = 0;
        int ans = 0;
        for (int i = 29; i >= 0; i--)
        {
            int bit = (x >> i) & 1;
            int target = 1 - bit;
            if (child[curr][target] != -1 && cnt[child[curr][target]] > 0)
            {
                ans |= (1 << i);
                curr = child[curr][target];
            }
            else
            {
                curr = child[curr][bit];
            }
        }
        return ans;
    }
};

