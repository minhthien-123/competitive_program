struct LazySegTree
{
    int n;
    std::vector<std::vector<int>> lazy;

    LazySegTree(int n)
    {
        this->n = n;
        lazy.assign(4 * n + 7, std::vector<int>(105));
        for (int i = 0; i < 4 * n + 7; i++)
        {
            for (int j = 1; j <= 100; j++)
            {
                lazy[i][j] = j;
            }
        }
    }

    void push(int id)
    {
        for (int i = 1; i <= 100; i++)
        {
            lazy[2 * id][i] = lazy[id][lazy[2 * id][i]];
            lazy[2 * id + 1][i] = lazy[id][lazy[2 * id + 1][i]];
        }
        for (int i = 1; i <= 100; i++)
        {
            lazy[id][i] = i;
        }
    }

    void update(int id, int l, int r, int u, int v, int x, int y)
    {
        if (v < l || r < u)
        {
            return;
        }
        if (u <= l && r <= v)
        {
            for (int i = 1; i <= 100; i++)
            {
                if (lazy[id][i] == x)
                {
                    lazy[id][i] = y;
                }
            }
            return;
        }
        push(id);
        int mid = (l + r) / 2;
        update(2 * id, l, mid, u, v, x, y);
        update(2 * id + 1, mid + 1, r, u, v, x, y);
    }

    void print(int id, int l, int r)
    {
        if (l == r)
        {
            std::cout << lazy[id][a[l]] << " ";
            return;
        }
        push(id);
        int mid = (l + r) / 2;
        print(2 * id, l, mid);
        print(2 * id + 1, mid + 1, r);
    }
};

