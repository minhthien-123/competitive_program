struct KMP
{
    std::vector<int> s;
    int n;
    std::vector<int> pi;

    KMP(std::vector<int> pattern)
    {
        s = pattern;
        n = sz(s);
        pi.assign(n, 0);
        for (int i = 1; i < n; i++)
        {
            int j = pi[i - 1];
            while (j > 0 && s[i] != s[j])
            {
                j = pi[j - 1];
            }
            if (s[i] == s[j])
            {
                j++;
            }
            pi[i] = j;
        }
    }

    int search(std::string t)
    {
        int m = t.length();
        int ans = 0;
        int j = 0;
        for (int i = 0; i < m; i++)
        {
            while (j > 0 && t[i] != s[j])
            {
                j = pi[j - 1];
            }
            if (t[i] == s[j])
            {
                j++;
            }
            if (j == n)
            {
                ans++;
                j = pi[j - 1];
            }
        }
        return ans;
    }
};

