struct FenwickTree
{
    std::vector<ll> bit;
    int n;

    FenwickTree(int n)
    {
        this->n = n;
        bit.assign(n, 0);
    }

    FenwickTree(std::vector<int> const &a) : FenwickTree((int)a.size())
    {
        for (int i = 0; i < (int)a.size(); i++)
        {
            add(i, a[i]);
        }
    }

    ll sum(int r)
    {
        if (r < 0)
            return 0;
        ll res = 0;
        for (; r >= 0; r = (r & (r + 1)) - 1)
        {
            res += bit[r];
            if (res >= (1ll << 60))
                res %= mod;
        }
        return res % mod;
    }

    ll sum(int l, int r)
    {
        if (l > r)
            return 0;
        ll res = sum(r) - sum(l - 1);
        res %= mod;
        if (res < 0)
            res += mod;
        return res;
    }

    void add(int idx, ll delta)
    {
        delta %= mod;
        if (delta < 0)
            delta += mod;
        for (; idx < n; idx = idx | (idx + 1))
        {
            bit[idx] += delta;
            if (bit[idx] >= mod)
                bit[idx] -= mod;
        }
    }
};

