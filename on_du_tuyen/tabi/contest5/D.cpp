    for (int i = 1; i <= n; i++)
    {
        v.pb(a[i]);
    }
    std::sort(v.begin(), v.end());
    v.erase(std::unique(v.begin(), v.end()), v.end());

    FenwickTree bit(sz(v));

    ll ans = 0;
    for (int i = 1; i <= n; i++)
    {
        int pos = std::lower_bound(v.begin(), v.end(), a[i]) - v.begin();
        ll s = bit.sum(pos - 1);
        ll tmp = (s + 1) % mod;
        bit.add(pos, tmp);
        ans = add(ans, tmp);
    }

    std::cout << ans;

    std::cerr << "\nTime elapsed: " << TIME << " s.\n";

    return 0;
