ll add(ll x, ll y)
{
    return (x + y + 2 * mod) % mod;
}
ll sub(ll x, ll y)
{
    return (x - y + 2 * mod) % mod;
}
ll mul(ll x, ll y)
{
    return (x * y) % mod;
}
ll power(ll x, ll y)
{
    if (y == 0)
    {
        return 1;
    }
    ll tmp = power(x, y / 2);
    if (y % 2 == 0)
    {
        return (tmp * tmp) % mod;
    }
    else
    {
        return tmp * tmp % mod * x % mod;
    }
}

std::vector<int> adj[maxn + 7];
int n;
int a[maxn + 7];

struct FenwickTree
{
    std::vector<int> bit;
    int n;

    FenwickTree(int n)
    {
        this->n = n;
        bit.assign(n, 0);
    }

    FenwickTree(std::vector<int> const &a) : FenwickTree(a.size())
    {
        for (int i = 0; i < a.size(); i++)
        {
            add(i, a[i]);
        }
    }

    ll sum(int r)
    {
        ll res = 0;
        for (; r >= 0; r = (r & (r + 1)) - 1)
        {
            res += bit[r];
        }
        return res;
    }

    ll sum(int l, int r)
    {
        return sum(r) - sum(l - 1);
    }

    void add(int idx, int delta)
    {
        for (; idx < n; idx = idx | (idx + 1))
        {
            bit[idx] += delta;
        }
    }
};

