ll add(ll x, ll y)
{
    return (x + y + 2 * mod) % mod;
}
ll sub(ll x, ll y)
{
    return (x - y + 2 * mod) % mod;
}
ll mul(ll x, ll y)
{
    return (x * y) % mod;
}
ll power(ll x, ll y)
{
    if (y == 0)
    {
        return 1;
    }
    ll tmp = power(x, y / 2);
    if (y % 2 == 0)
    {
        return (tmp * tmp) % mod;
    }
    else
    {
        return tmp * tmp % mod * x % mod;
    }
}

std::vector<int> adj[maxn + 7];
int n, k;

int solve(ll n, ll k)
{
    if (n == 1)
    {
        return 1;
    }
    ll m = n / 2;
    if (k <= m)
    {
        return 2 * k;
    }
    ll s = n - m;
    ll r = solve(s, k - m);
    if (n % 2 == 0)
    {
        return 2 * r - 1;
    }
    else
    {
        ll pos = ((s + r - 2) % s) + 1;
        return 2 * pos - 1;
    }
}

