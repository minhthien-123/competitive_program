const int mod = 1e9 + 7;
const int inv = (mod + 1) / 2;
const int lg = 20;

ll add(ll x, ll y)
{
    return (x + y + 2 * mod) % mod;
}
ll sub(ll x, ll y)
{
    return (x - y + 2 * mod) % mod;
}
ll mul(ll x, ll y)
{
    return (x * y) % mod;
}
ll power(ll x, ll y)
{
    if (y == 0)
    {
        return 1;
    }
    ll tmp = power(x, y / 2);
    if (y % 2 == 0)
    {
        return (tmp * tmp) % mod;
    }
    else
    {
        return tmp * tmp % mod * x % mod;
    }
}

std::vector<int> adj[maxn + 7];
int n, k;
ll ans = 0;
int a[maxn + 7];
int bit[maxn + 7];
int L[maxn + 7];
int R[maxn + 7];

void update(int idx, int val)
{
    for (; idx <= n; idx += idx & -idx)
    {
        bit[idx] += val;
    }
}

int get(int idx)
{
    int res = 0;
    for (; idx > 0; idx -= idx & -idx)
    {
        res += bit[idx];
    }
    return res;
}

