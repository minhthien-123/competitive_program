const int maxn = 2e5 + 5;
const ll inf = 1e18;

struct tree
{
    int v1, c1;
    int v2, c2;
};

int n, q;
int a[maxn];
tree st[4 * maxn];

tree merge(const tree l, const tree r)
{
    tree res;

    if (l.v1 > r.v1)
    {
        res.v1 = l.v1;
        res.c1 = l.c1;
        if (l.v2 > r.v1)
        {
            res.v2 = l.v2;
            res.c2 = l.c2;
        }
        else if (l.v2 < r.v1)
        {
            res.v2 = r.v1;
            res.c2 = r.c1;
        }
        else
        {
            res.v2 = r.v1;
            res.c2 = l.c2 + r.c1;
        }
    }
    else if (l.v1 < r.v1)
    {
        res.v1 = r.v1;
        res.c1 = r.c1;
        if (r.v2 > l.v1)
        {
            res.v2 = r.v2;
            res.c2 = r.c2;
        }
        else if (r.v2 < l.v1)
        {
            res.v2 = l.v1;
            res.c2 = l.c1;
        }
        else
        {
            res.v2 = l.v1;
            res.c2 = r.c2 + l.c1;
        }
    }
    else
    {
        res.v1 = l.v1;
        res.c1 = l.c1 + r.c1;
        if (l.v2 > r.v2)
        {
            res.v2 = l.v2;
            res.c2 = l.c2;
        }
        else if (l.v2 < r.v2)
        {
            res.v2 = r.v2;
            res.c2 = r.c2;
        }
        else
        {
            res.v2 = l.v2;
            res.c2 = l.c2 + r.c2;
        }
    }
    return res;
}

void build(int id, int l, int r)
{
    if (l == r)
    {
        st[id] = {a[l], 1, -1, 0};
        return;
    }
    int mid = (l + r) >> 1;
    build(id * 2, l, mid);
    build(id * 2 + 1, mid + 1, r);
    st[id] = merge(st[id * 2], st[id * 2 + 1]);
}

void update(int id, int l, int r, int pos, int val)
{
    if (pos < l || pos > r)
    {
        return;
    }
    if (l == r)
    {
        st[id] = {val, 1, -1, 0};
        return;
    }
    int mid = (l + r) >> 1;
    update(id * 2, l, mid, pos, val);
    update(id * 2 + 1, mid + 1, r, pos, val);
    st[id] = merge(st[id * 2], st[id * 2 + 1]);
}

tree get(int id, int l, int r, int u, int v)
{
    if (v < l || u > r)
    {
        return {-1, 0, -1, 0};
    }
    if (u <= l && r <= v)
    {
        return st[id];
    }
    int mid = (l + r) >> 1;
    return merge(get(id * 2, l, mid, u, v), get(id * 2 + 1, mid + 1, r, u, v));
}

