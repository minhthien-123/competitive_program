const int maxn = 1e7;
const ll inf = 1e18;
const int mod = 1e9 + 7;
const int inv = (mod + 1) / 2;
const int lg = 20;

ll add(ll x, ll y)
{
    return (x + y + 2 * mod) % mod;
}
ll sub(ll x, ll y)
{
    return (x - y + 2 * mod) % mod;
}
ll mul(ll x, ll y)
{
    return (x * y) % mod;
}
ll power(ll x, ll y)
{
    if (y == 0)
    {
        return 1;
    }
    ll tmp = power(x, y / 2);
    if (y % 2 == 0)
    {
        return (tmp * tmp) % mod;
    }
    else
    {
        return tmp * tmp % mod * x % mod;
    }
}
struct Node
{
    int val;
    int or_val;
};

int n, k;
ll x, a, b, c;

Node s1[maxn], s2[maxn];
int t1 = 0, t2 = 0;

void push(int val)
{
    int cur = val;
    if (t1 > 0)
    {
        cur |= s1[t1].or_val;
    }
    t1++;
    s1[t1] = {val, cur};
}

void pop()
{
    if (t2 == 0)
    {
        while (t1 > 0)
        {
            int val = s1[t1].val;
            t1--;

            int cur = val;
            if (t2 > 0)
            {
                cur |= s2[t2].or_val;
            }
            t2++;
            s2[t2] = {val, cur};
        }
    }
    if (t2 > 0)
        t2--;
}

int get_or()
{
    int res = 0;
    if (t1 > 0)
    {
        res |= s1[t1].or_val;
    }
    if (t2 > 0)
    {
        res |= s2[t2].or_val;
    }
    return res;
}

