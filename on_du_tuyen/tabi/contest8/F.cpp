ll add(ll x, ll y)
{
    return (x + y + 2 * mod) % mod;
}
ll sub(ll x, ll y)
{
    return (x - y + 2 * mod) % mod;
}
ll mul(ll x, ll y)
{
    return (x * y) % mod;
}
ll power(ll x, ll y)
{
    if (y == 0)
    {
        return 1;
    }
    ll tmp = power(x, y / 2);
    if (y % 2 == 0)
    {
        return (tmp * tmp) % mod;
    }
    else
    {
        return tmp * tmp % mod * x % mod;
    }
}

std::vector<int> adj[maxn + 7];
int n, q;
ll a[maxn + 7];
ll st[4 * maxn + 7];

void build(int id, int l, int r)
{
    if (l == r)
    {
        st[id] = a[l];
        return;
    }
    int mid = l + r >> 1;
    build(id * 2, l, mid);
    build(id * 2 + 1, mid + 1, r);
    st[id] = std::__gcd(st[id * 2], st[id * 2 + 1]);
}

void update(int id, int l, int r, int pos, int val)
{
    if (pos < l || pos > r)
        return;
    if (l == r)
    {
        st[id] = val;
        return;
    }
    int mid = l + r >> 1;
    update(id * 2, l, mid, pos, val);
    update(id * 2 + 1, mid + 1, r, pos, val);
    st[id] = std::__gcd(st[id * 2], st[id * 2 + 1]);
}

int get(int id, int l, int r, int u, int v, int x)
{
    if (v < l || r < u)
        return 0;
    if (st[id] % x == 0)
        return 0;
    if (l == r)
        return 1;

    int mid = l + r >> 1;

    if (u <= l && r <= v)
    {
        if (st[2 * id] % x != 0 && st[2 * id + 1] % x != 0)
        {
            return 2;
        }
        if (st[2 * id] % x != 0)
        {
            return get(2 * id, l, mid, u, v, x);
        }
        else
        {
            return get(2 * id + 1, mid + 1, r, u, v, x);
        }
    }

    int get1 = get(2 * id, l, mid, u, v, x);
    int get2 = get(2 * id + 1, mid + 1, r, u, v, x);

    return get1 + get2;
}

