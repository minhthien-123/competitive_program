const int mod = 1e9 + 7;
const int inv = (mod + 1) / 2;
const int lg = 20;

ll add(ll x, ll y)
{
    return (x + y + 2 * mod) % mod;
}
ll sub(ll x, ll y)
{
    return (x - y + 2 * mod) % mod;
}
ll mul(ll x, ll y)
{
    return (x * y) % mod;
}
ll power(ll x, ll y)
{
    if (y == 0)
    {
        return 1;
    }
    ll tmp = power(x, y / 2);
    if (y % 2 == 0)
    {
        return (tmp * tmp) % mod;
    }
    else
    {
        return tmp * tmp % mod * x % mod;
    }
}

std::vector<int> adj[maxn + 7];
int n, dp[maxn + 7];
int root;
int ans = 0;

void dfs(int u, int p)
{
    vi vec;
    for (int v : adj[u])
    {
        if (v == p)
        {
            continue;
        }
        dfs(v, u);
        vec.pb(dp[v]);
    }

    if (vec.empty())
    {
        dp[u] = 0;
        return;
    }

    std::sort(vec.begin(), vec.end(), std::greater<int>());

    int best = 0;
    for (int i = 0; i < sz(vec); i++)
    {
        best = std::max(best, (i + 1) + vec[i]);
    }
    dp[u] = best;
}

