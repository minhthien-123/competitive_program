const int maxn = 1e5;
const ll inf = 1e18;
const int mod = 1e9 + 7;
const int inv = (mod + 1) / 2;
const int lg = 20;

ll add(ll x, ll y)
{
    return (x + y + 2 * mod) % mod;
}
ll sub(ll x, ll y)
{
    return (x - y + 2 * mod) % mod;
}
ll mul(ll x, ll y)
{
    return (x * y) % mod;
}
ll power(ll x, ll y)
{
    if (y == 0)
    {
        return 1;
    }
    ll tmp = power(x, y / 2);
    if (y % 2 == 0)
    {
        return (tmp * tmp) % mod;
    }
    else
    {
        return tmp * tmp % mod * x % mod;
    }
}

std::vector<int> adj[maxn + 7];
int n, m;
int par[maxn + 7], sz[maxn + 7];
int c[maxn + 7];
std::map<int, int> cnt[maxn + 7];

void init(int u)
{
    par[u] = u;
    sz[u] = 1;
    cnt[u][c[u]] = 1;
}

int acs(int u)
{
    if (u == par[u])
    {
        return u;
    }
    return par[u] = acs(par[u]);
}

int ans = 0;

void join(int u, int v)
{
    u = acs(u);
    v = acs(v);
    if (u != v)
    {
        if (sz[u] < sz[v])
        {
            std::swap(u, v);
        }
        par[v] = u;
        sz[u] += v;
        for (auto p : cnt[v])
        {
            auto it = cnt[u].find(p.fi);
            if (it != cnt[u].end())
            {
                ans += p.se * it->se;
            }
        }
        for (auto p : cnt[v])
        {
            cnt[u][p.fi] += p.se;
        }
    }
}

