ll add(ll x, ll y)
{
    return (x + y + 2 * mod) % mod;
}
ll sub(ll x, ll y)
{
    return (x - y + 2 * mod) % mod;
}
ll mul(ll x, ll y)
{
    return (x * y) % mod;
}
ll power(ll x, ll y)
{
    if (y == 0)
    {
        return 1;
    }
    ll tmp = power(x, y / 2);
    if (y % 2 == 0)
    {
        return (tmp * tmp) % mod;
    }
    else
    {
        return tmp * tmp % mod * x % mod;
    }
}

int n, m, k, t;
int a[maxn + 7][maxn + 7];
int f[maxn + 7][maxn + 7];
int mx[maxn + 7][maxn + 7];
int lmax[maxn + 7], rpos[maxn + 7], rmax[maxn + 7];
int topmax[maxn + 7], botpos[maxn + 7], botmax[maxn + 7];

void sub1()
{
    int ans = -inf;

    for (int i = 1; i <= n; i++)
    {
        for (int j = 1; j <= m; j++)
        {
            for (int u = n; u >= i; u--)
            {
                for (int v = m; v >= j; v--)
                {
                    int sum = f[u][v] - f[i - 1][v] - f[u][j - 1] + f[i - 1][j - 1];
                    // std::cout << i << " " << j << " " << u << " " << v << " " << sum << "\n";
                    int x = u - i + 1;
                    int y = v - j + 1;
                    int tmp = x * y;
                    if (sum <= t && x == y)
                    {
                        // std::cout << i << " " << j << " " << u << " " << v << " " << sum << "\n";
                        ans = std::max(ans, tmp);
                    }
                }
            }
        }
    }

    if (ans == -inf)
    {
        ans = 0;
    }

    std::cout << ans << "\n";
}

bool check(int mid)
{
    for (int i = 1; i <= n - mid; i++)
    {
        for (int j = 1; j <= m - mid; j++)
        {
            int u = i + mid;
            int v = j + mid;
            int sum = f[u][v] - f[i - 1][v] - f[u][j - 1] + f[i - 1][j - 1];
            // std::cout << i << " " << j << " " << u << " " << v << " " << sum << "\n";
            if (sum <= t)
            {
                return true;
            }
        }
    }
    return false;
}

void sub2()
{
    int ans = 0;
    for (int i = 1; i <= n; i++)
    {
        for (int j = 1; j <= m; j++)
        {
            int l = 1, r = std::min(n - i + 1, m - j + 1), res = 0;
            while (l <= r)
            {
                int mid = (l + r) >> 1;
                int u = i + mid - 1, v = j + mid - 1;
                int sum = f[u][v] - f[i - 1][v] - f[u][j - 1] + f[i - 1][j - 1];
                if (sum <= t)
                {
                    res = mid;
                    l = mid + 1;
                }
                else
                    r = mid - 1;
            }
            mx[i][j] = res;
            ans = std::max(ans, res);
        }
    }
    std::cout << ans * ans << "\n";
}

void sub3()
{
    int best = 0;

    for (int i = 1; i <= n; i++)
    {
        for (int j = 1; j <= m; j++)
        {
            int l = 1, r = std::min(n - i + 1, m - j + 1), len = 0;
            while (l <= r)
            {
                int mid = (l + r) >> 1;
                int u = i + mid - 1, v = j + mid - 1;
                int sum = f[u][v] - f[i - 1][v] - f[u][j - 1] + f[i - 1][j - 1];
                if (sum <= t)
                {
                    len = mid;
                    l = mid + 1;
                }
                else
                {
                    r = mid - 1;
                }
            }
            mx[i][j] = len;
        }
    }

    for (int i = 1; i <= n; i++)
    {
        topmax[i] = 0;
        botpos[i] = 0;
    }
    for (int i = 1; i <= n; i++)
    {
        for (int j = 1; j <= m; j++)
        {
            int len = mx[i][j];
            if (len > 0)
            {
                int x = i + len - 1;
                int area = len * len;
                topmax[x] = std::max(topmax[x], area);
                botpos[i] = std::max(botpos[i], area);
            }
        }
    }
    for (int i = 1; i <= n; i++)
    {
        topmax[i] = std::max(topmax[i], topmax[i - 1]);
    }
    botmax[n + 1] = 0;
    for (int i = n; i >= 1; i--)
    {
        botmax[i] = std::max(botpos[i], botmax[i + 1]);
    }

    for (int i = 1; i < n; i++)
    {
        best = std::max(best, topmax[i] + botmax[i + 1]);
    }

    for (int j = 1; j <= m; j++)
    {
        lmax[j] = 0;
        rpos[j] = 0;
    }
    for (int i = 1; i <= n; i++)
    {
        for (int j = 1; j <= m; j++)
        {
            int len = mx[i][j];
            if (len > 0)
            {
                int y = j + len - 1;
                int area = len * len;
                lmax[y] = std::max(lmax[y], area);
                rpos[j] = std::max(rpos[j], area);
            }
        }
    }
    for (int j = 1; j <= m; j++)
    {
        lmax[j] = std::max(lmax[j], lmax[j - 1]);
    }
    rmax[m + 1] = 0;
    for (int j = m; j >= 1; j--)
    {
        rmax[j] = std::max(rpos[j], rmax[j + 1]);
    }

    for (int j = 1; j < m; j++)
    {
        best = std::max(best, lmax[j] + rmax[j + 1]);
    }

    std::cout << best << "\n";
}

